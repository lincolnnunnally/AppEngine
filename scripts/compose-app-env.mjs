#!/usr/bin/env node
import fs from "node:fs";
import os from "node:os";
import path from "node:path";

function parseArgs(argv) {
  const args = {};
  for (let index = 0; index < argv.length; index += 1) {
    const item = argv[index];
    if (!item.startsWith("--")) continue;
    const key = item.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith("--")) {
      args[key] = "true";
    } else {
      args[key] = next;
      index += 1;
    }
  }
  return args;
}

function expandHome(input) {
  if (!input) return input;
  if (input === "~") return os.homedir();
  if (input.startsWith("~/")) return path.join(os.homedir(), input.slice(2));
  return input;
}

function readEnvFile(filePath, { required = true } = {}) {
  const resolved = path.resolve(expandHome(filePath));
  if (!fs.existsSync(resolved)) {
    if (required) throw new Error(`Env file not found: ${resolved}`);
    return { path: resolved, values: new Map(), comments: [] };
  }

  const values = new Map();
  const comments = [];
  const text = fs.readFileSync(resolved, "utf8");
  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line) continue;
    if (line.startsWith("#")) {
      comments.push(line);
      continue;
    }

    const equalsIndex = line.indexOf("=");
    if (equalsIndex === -1) continue;
    const key = line.slice(0, equalsIndex).trim();
    const value = line.slice(equalsIndex + 1).trim();
    if (!key || key.startsWith("export ")) continue;
    values.set(key, value);
  }

  return { path: resolved, values, comments };
}

function splitList(value) {
  return String(value || "")
    .split(/[\s,]+/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function unquote(value) {
  const text = String(value ?? "");
  if ((text.startsWith('"') && text.endsWith('"')) || (text.startsWith("'") && text.endsWith("'"))) {
    return text.slice(1, -1);
  }
  return text;
}

function resolveValue(rawValue, merged) {
  const text = String(rawValue ?? "");
  const exactMatch = text.match(/^\$\{([A-Z0-9_]+)\}$/);
  if (exactMatch) return merged.get(exactMatch[1]) || "";
  return text.replace(/\$\{([A-Z0-9_]+)\}/g, (_match, key) => unquote(merged.get(key) || ""));
}

function renderEnv(values, sourceNote) {
  const lines = [
    "# Generated by AppEngine env composer.",
    "# Do not commit real secret values.",
    `# Source: ${sourceNote}`,
    "",
  ];

  for (const [key, value] of values) {
    lines.push(`${key}=${value}`);
  }
  lines.push("");
  return lines.join("\n");
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const localSharedPath = path.resolve(".env.shared.local");
  const defaultSharedPath = process.env.APPENGINE_SHARED_ENV_FILE
    || (fs.existsSync(localSharedPath) ? localSharedPath : "~/Documents/Codex/private-env/appengine.shared.env");

  const sharedPath = args.shared || defaultSharedPath;
  const profilePath = args.profile;
  const outPath = args.out;

  if (!profilePath) {
    throw new Error("Missing --profile <path-to-app-env-profile>");
  }
  if (!outPath && args.check !== "true") {
    throw new Error("Missing --out <target-env-file> or --check true");
  }

  const shared = readEnvFile(sharedPath, { required: args.allowMissingShared !== "true" });
  const profile = readEnvFile(profilePath);
  const requestedSharedKeys = splitList(profile.values.get("APPENGINE_SHARED_ENV_KEYS"));
  const requiredKeys = splitList(profile.values.get("APPENGINE_REQUIRED_ENV_KEYS"));

  const merged = new Map();
  for (const key of requestedSharedKeys) {
    if (shared.values.has(key)) merged.set(key, shared.values.get(key));
  }

  for (const [key, rawValue] of profile.values) {
    if (key.startsWith("APPENGINE_")) continue;
    merged.set(key, resolveValue(rawValue, new Map([...shared.values, ...merged])));
  }

  const missing = requiredKeys.filter((key) => !unquote(merged.get(key) || "").trim());
  if (missing.length > 0) {
    throw new Error(`Missing required env values: ${missing.join(", ")}`);
  }

  if (args.check === "true") {
    console.log(`Env profile OK: ${profile.path}`);
    console.log(`Shared source: ${shared.path}`);
    console.log(`Keys selected: ${merged.size}`);
    return;
  }

  const resolvedOut = path.resolve(expandHome(outPath));
  fs.mkdirSync(path.dirname(resolvedOut), { recursive: true });
  fs.writeFileSync(resolvedOut, renderEnv(merged, `${shared.path} + ${profile.path}`));
  console.log(`Wrote env file: ${resolvedOut}`);
  console.log(`Keys written: ${merged.size}`);
}

try {
  main();
} catch (error) {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
}
