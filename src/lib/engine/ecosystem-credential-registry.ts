// ─── Live status resolution (read-only; never returns values) ────────────────

const VERCEL_API = "https://api.vercel.com";

function vercelToken() {
  return process.env.VERCEL_TOKEN?.trim();
}

export function hasVercelReadApi() {
  return Boolean(vercelToken());
}

export type CredentialStatus = "set" | "missing" | "manual" | "known";

// Returns a per-key status map keyed by `${slug}:${envVar}`. Vercel-hosted keys
// are resolved live from the env-NAMES API (production target); manual/public
// keys resolve statically. Values are never fetched or returned.
export async function getCredentialStatuses(): Promise<Record<string, CredentialStatus>> {
  const statuses: Record<string, CredentialStatus> = {};
  const token = vercelToken();

  // Cache the set-of-names per Vercel project so we hit each project once.
  const projectNames = new Map<string, Set<string>>();

  async function namesFor(projectId: string): Promise<Set<string> | null> {
    if (projectNames.has(projectId)) {
      return projectNames.get(projectId)!;
    }
    if (!token) {
      return null;
    }
    try {
      const res = await fetch(`${VERCEL_API}/v9/projects/${projectId}/env`, {
        headers: { authorization: `Bearer ${token}` },
      });
      if (!res.ok) {
        return null;
      }
      const data = (await res.json()) as { envs?: Array<{ key?: string; target?: string[] }> };
      const set = new Set(
        (data.envs || [])
          .filter((e) => Array.isArray(e.target) && e.target.includes("production") && e.key)
          .map((e) => e.key as string)
      );
      projectNames.set(projectId, set);
      return set;
    } catch {
      return null;
    }
  }

  for (const group of CREDENTIAL_REGISTRY) {
    for (const key of group.keys) {
      const id = `${group.slug}:${key.envVar}`;
      if (key.statusMode === "public") {
        statuses[id] = "known";
      } else if (key.statusMode === "manual") {
        statuses[id] = "manual";
      } else {
        // vercel_env
        const names = group.vercelProjectId ? await namesFor(group.vercelProjectId) : null;
        statuses[id] = names ? (names.has(key.envVar) ? "set" : "missing") : "manual";
      }
    }
  }

  return statuses;
}

// Forward-looking hook (free): the future ops/attention queue can import this to
// list what the owner still owes a given app.
export function missingCredentialsForApp(slug: string, statuses: Record<string, CredentialStatus>): CredentialEntry[] {
  const group = CREDENTIAL_REGISTRY.find((g) => g.slug === slug);
  if (!group) {
    return [];
  }
  return group.keys.filter((key) => {
    const status = statuses[`${slug}:${key.envVar}`];
    return status === "missing" && key.whoProvides === "lincoln";
  });
}

// ─── Ops attention queue ─────────────────────────────────────────────────────

export type CredentialAttentionItem = {
  slug: string;
  appName: string;
  displayName: string;
  envVar: string;
  host: CredentialHost;
  location: string;
  priority: AttentionPriority;
  action: string;
  loginCritical: boolean;
};

const PRIORITY_ORDER: Record<AttentionPriority, number> = { blocker: 0, recommended: 1, optional: 2 };

// The owner action items the ops dashboard surfaces: every registry key carrying
// an `attention` marker whose condition currently holds (always, or Vercel-status
// missing). Sorted blockers-first. No secret values — names, locations, actions.
export async function getCredentialAttentionItems(): Promise<CredentialAttentionItem[]> {
  const statuses = await getCredentialStatuses();
  const items: CredentialAttentionItem[] = [];

  for (const group of CREDENTIAL_REGISTRY) {
    for (const key of group.keys) {
      if (!key.attention) {
        continue;
      }
      const status = statuses[`${group.slug}:${key.envVar}`];
      if (key.attention.when === "missing" && status !== "missing") {
        continue;
      }
      items.push({
        slug: group.slug,
        appName: group.name,
        displayName: key.displayName,
        envVar: key.envVar,
        host: key.host,
        location: key.location,
        priority: key.attention.priority,
        action: key.attention.action,
        loginCritical: Boolean(key.loginCritical),
      });
    }
  }

  items.sort((a, b) => PRIORITY_ORDER[a.priority] - PRIORITY_ORDER[b.priority]);
  return items;
}
