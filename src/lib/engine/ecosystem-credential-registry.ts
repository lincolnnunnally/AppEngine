// Ecosystem Credential Registry — the canonical, per-app list of the keys every
// Lincoln-owned app needs, WHERE each one lives (which Vercel project / Render
// service / Supabase project), the EXACT variable name the app reads, and who
// must supply it. Rendered read-only in the cockpit at /credentials so the owner
// can see "which key -> which slot -> which variable" instead of a vague
// "we need a Render key".
//
// SERVER ONLY. Never import from a client component. No secret VALUES live here —
// only names, targets, and public values (URLs, project ids, publishable keys).
// Live "set / missing" status for Vercel-hosted keys is resolved at request time
// from the Vercel env-names API (same read-only pattern as integrations-config.ts);
// Render/Supabase values can't be read from here (no Render API key), so those are
// marked "manual" and the owner confirms them in the provider dashboard.
//
// Field vocabulary mirrors source-of-truth/deployment-environment-standard.md so
// there is ONE credential vocabulary across the ecosystem, not two.

export type CredentialHost = "vercel" | "render" | "supabase" | "provider";

// scope mirrors the deployment-environment-standard record shape.
export type CredentialScope =
  | "identity" // auth/sign-in
  | "database"
  | "frontend_public" // NEXT_PUBLIC_/VITE_ values baked into the client bundle
  | "server" // backend-only secret
  | "provider" // third-party API key (Stripe, Twilio, Resend, Render…)
  | "config"; // non-secret operational flags/URLs

// How the /credentials view resolves this key's set/missing status.
export type CredentialStatusMode =
  | "vercel_env" // checkable now via the Vercel env-names API (needs vercelProjectId)
  | "manual" // Render/Supabase-side — not checkable here; owner confirms in the dashboard
  | "public"; // a known non-secret value; always shown, always "set"

export type WhoProvides =
  | "lincoln" // owner must create/paste it
  | "auto" // generated by the host (Render generateValue, engine vault)
  | "already-set"; // known to be in place

// Priority of an open owner action surfaced in the ops attention queue.
export type AttentionPriority = "blocker" | "recommended" | "optional";

// Attach to a CredentialEntry that is a CURRENT owner action item so it shows up
// on the ops dashboard, not only on /credentials. `when: "missing"` surfaces it
// only when the (Vercel) status actually reads missing; `when: "always"` surfaces
// it regardless — for Render/provider keys we can't read from here but know are
// open (e.g. a service that isn't stood up yet).
export type CredentialAttention = {
  when: "missing" | "always";
  priority: AttentionPriority;
  action: string; // the precise next step, in plain language
};

export type CredentialEntry = {
  displayName: string; // uniquely-named, unambiguous label
  envVar: string; // EXACT variable the app reads
  host: CredentialHost;
  location: string; // the precise slot the value goes in
  scope: CredentialScope;
  secret: boolean;
  publicValue?: string; // only for non-secret values
  whoProvides: WhoProvides;
  purpose: string; // one line: what breaks without it
  statusMode: CredentialStatusMode;
  sourceFile?: string; // where this contract is declared
  loginCritical?: boolean; // true => directly gates someone logging in
  attention?: CredentialAttention; // present => a current owner action item
};

export type CredentialAppGroup = {
  slug: string; // matches app-portfolio-registry.ts slugs
  name: string;
  summary: string; // one line: current login/auth reality
  vercelProjectId?: string; // enables live Vercel status for this group's vercel_env keys
  renderService?: string; // the Render service name, when the app has a backend there
  keys: CredentialEntry[];
};

