// events-scheduling — events, RSVPs/registrations, facilities, and facility
// booking/scheduling, ported from ChurchConnect's real code:
//   - backend/routes/events.py — the event model (title/description/start_date/
//     end_date/location/location_type/category/capacity/registration_required/
//     cost/is_free/status/is_published…), church-scoped listing with status /
//     category / date-range filters sorted by start_date, registration_count
//     enrichment, upcoming-published feed, and register_for_event's capacity +
//     duplicate-registration guards.
//   - backend/routes/facilities.py — the facility model (name/description/
//     capacity/amenities/hourly_rate/requires_approval), create_booking's
//     overlap conflict check (start < end AND end > start against pending/
//     approved), requires_approval → pending vs approved status, and the
//     availability check for a date.
//   - backend/routes/event_management.py — attendee/checklist concepts folded
//     into the registration + attendee status vocabulary (registered /
//     checked_in / cancelled / no_show).
// Generalized off the church framing (church_id → owner user, "General" default
// category) into plain copy so it drops into any app. Runs on the app owner's
// own database with graceful sample-data fallback, feature-flagged
// (FEATURE_SCHEDULING); customers browse events + RSVP, admins create/manage
// events and facilities and approve bookings.

