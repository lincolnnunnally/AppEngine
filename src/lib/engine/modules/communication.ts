    "// (the touchpoint loop in publish_broadcast). Sends stay 'queued' until the",
    "// owner's provider actually delivers them (process-queue), so this is safe with",
    "// no keys configured — it just reports the channel as not ready.",
    "export async function publishBroadcast(",
    "  id: string,",
    "  recipients: Recipient[] = []",
