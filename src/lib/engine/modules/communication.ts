    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessAdmin } from "@/lib/auth/roles";',
    'import { createBroadcast, commsEnabled } from "@/lib/db/communication";',
    'import type { BroadcastChannel } from "@/lib/db/communication";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "// Progressive-enhancement handler for the compose form on /comms.",
    "export async function POST(request: Request) {",
    "  if (!commsEnabled()) return NextResponse.redirect(new URL(\"/comms\", request.url));",
