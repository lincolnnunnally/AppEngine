// communication — messaging pipes: broadcasts + a queued notification pipeline
// (email/SMS to targeted recipients), ported from ChurchConnect's real
// Communication Command Center: src/components/BroadcastHub.tsx (the
// broadcast/newsletter tabs + History), backend/routes/broadcasting.py
// (event_broadcasts create/list/publish + notification_queue send-email/send-sms),
// backend/routes/email_service.py (send_email_smtp + /api/email/send-bulk +
// /api/email/process-queue + email_log) and backend/routes/church_sms.py
// (per-owner phone send + message_balance + sms_logs).
//
// We keep the actual model — a broadcast with title/date/time/location/
// description, a target audience, a status lifecycle (draft -> scheduled), and
// per-channel reach stats; plus a notification queue row (channel email|sms, a
// recipient, subject/body or message, status queued|sent|failed). Publishing a
// broadcast fans it out into queued notifications, exactly as
// publish_broadcast() does. The email/SMS providers are abstracted behind the
// app owner's own env keys (SMTP_* / SMS_*); when they are absent, sends are
// queued and clearly reported as "not configured yet" — never a provider name
// shown to end users. Runs on the app owner's own database with graceful
// sample-data fallback, feature-flagged (FEATURE_COMMS), customers browse,
// admins compose and publish.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

