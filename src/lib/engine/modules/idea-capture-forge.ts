// idea-capture-forge — capture an idea as a note, then forge it into shippable
// outputs (blog post, thread, script, devotional, ...). Ported from the real
// "ideas" app:
//   - the note/idea capture model (backend/notes.py NoteOut: id, user_id, title,
//     content, content_text, type[text|voice|meeting|image|imported], source,
//     tags, ai_summary, ai_category, status, created_at/updated_at) — kept the
//     text-capture core; voice/photo are optional capture doors in that app, so
//     type is retained and the field carried, capture stays text-first here.
//   - Polish (backend/ai.py polish_text + notes.py polish_note): a copy-editor
//     pass that fixes spelling/grammar only, preserves voice, and reports
//     `changed` — ported verbatim as the polish prompt + no-op detection.
//   - Forge It (backend/forge.py + forge_prompts.py + src/lib/forge.js +
//     ForgeModal.jsx): the 12 OUTPUT_TYPES with their blurbs + LENGTH_HINTS, the
//     _GENERAL_RULES + per-type _SPECS system/instruction prompts, CLAUDE_TYPES /
//     GPT_TYPES model routing, build_prompt assembly (source text + ai summary +
//     tags + audience/tone/length), strict-JSON {title, markdown} parsing with a
//     first-line title fallback, _md_to_html, and the forged_outputs record
//     (source_note_id, output_type, title, content, content_markdown, model_used,
//     prompt_context, version, parent_version_id, status, error) — including
//     regenerate-as-a-new-version.
// Generalized off the pastor/coach framing into plain warm copy so it drops into
// any app. Runs on the app owner's own database with graceful sample-data
// fallback, feature-flagged (FEATURE_IDEAS). The AI owner key is read from an env
// var; when unset, capture + polish + forge still work (polish is a no-op, forge
// emits a clean draft scaffold) so the UI is never blocked.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

