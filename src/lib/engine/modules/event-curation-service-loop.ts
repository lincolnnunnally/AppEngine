function eventActionsFile(): GeneratedModuleFile {
  return file("src/app/events/[id]/actions.tsx", [
    '"use client";',
    "",
    'import { useState } from "react";',
    'import { useRouter } from "next/navigation";',
    "",
    "type Props = {",
    "  eventId: string;",
    "  status: string;",
    "  isHost: boolean;",
    "  isAdmin: boolean;",
    "  isAttending: boolean;",
    "  isFull: boolean;",
    "  featured: boolean;",
    "};",
    "",
    "export function EventActions(props: Props) {",
    "  const router = useRouter();",
    "  const [busy, setBusy] = useState(false);",
    "  const [error, setError] = useState<string | null>(null);",
    "",
    "  async function call(url: string, method: string, body?: Record<string, unknown>) {",
    "    setBusy(true);",
    "    setError(null);",
    "    try {",
    "      const response = await fetch(url, {",
    "        method,",
    '        headers: body ? { "Content-Type": "application/json" } : undefined,',
    "        body: body ? JSON.stringify(body) : undefined",
    "      });",
    "      const data = await response.json().catch(() => ({}));",
    "      if (!response.ok || !data.ok) {",
    '        setError(data.message || "Something went wrong.");',
    "        setBusy(false);",
    "        return;",
    "      }",
    "      router.refresh();",
    "      setBusy(false);",
    "    } catch {",
    '      setError("Could not reach the server. Try again.");',
    "      setBusy(false);",
    "    }",
    "  }",
    "",
    "  const active = props.status === \"active\";",
    "",
    "  return (",
    '    <section className="stack">',
    "      {error ? <p className=\"note error\" role=\"alert\">{error}</p> : null}",
    "      {active && !props.isHost && !props.isAttending && !props.isFull ? (",
    "        <button className=\"button primary\" disabled={busy} onClick={() => call(`/api/events/${props.eventId}/rsvp`, \"POST\")}>{busy ? \"…\" : \"I will be there\"}</button>",
    "      ) : null}",
    "      {props.isAttending && !props.isHost ? (",
    "        <button className=\"button\" disabled={busy} onClick={() => call(`/api/events/${props.eventId}/rsvp`, \"DELETE\")}>Cancel my RSVP</button>",
    "      ) : null}",
    "      {props.isFull && !props.isAttending ? <p className=\"note\">This event is full.</p> : null}",
    "      {props.isAdmin ? (",
    '        <div className="stack">',
    "          <button className=\"button\" disabled={busy} onClick={() => call(`/api/events/${props.eventId}`, \"PATCH\", { featured: !props.featured })}>{props.featured ? \"Unfeature\" : \"Feature\"}</button>",
    "          {active ? (",
    "            <button className=\"button\" disabled={busy} onClick={() => call(`/api/events/${props.eventId}`, \"PATCH\", { status: \"cancelled\" })}>Cancel event</button>",
    "          ) : (",
    "            <button className=\"button\" disabled={busy} onClick={() => call(`/api/events/${props.eventId}`, \"PATCH\", { status: \"active\" })}>Publish</button>",
    "          )}",
    "        </div>",
    "      ) : null}",
    "    </section>",
    "  );",
    "}"
  ]);
}

