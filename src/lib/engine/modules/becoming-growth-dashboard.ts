// becoming-growth-dashboard — a personal growth dashboard (reflection journal,
// growth goals, weekly check-ins, an alignment score, and a streak with a
// streak-pause), ported from Kindred-Connection's real "Becoming" system
// (backend/routers/becoming.py compute_alignment + becoming.py's daily prompt +
// backend/routers/streak.py pause/resume + backend/server.py /journal, /goals,
// /checkins, /milestones). We keep its actual model — journal entries (title,
// content, mood), goals (title, description, target_date, progress, completed →
// milestone at 100%), weekly check-ins (mood, note), a per-user alignment history
// keyed by ISO week, and the exact 0–100 alignment scoring + streak-counting
// (weeks at/above a threshold, skipping paused weeks). Generalized from Kindred's
// dating context to any app's personal-growth surface. Runs on the app owner's own
// database with a graceful sample-data fallback, feature-flagged (FEATURE_GROWTH),
// customers see their own data, admins can adjust the alignment threshold.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

