    "    const journals = Number((jr[0] as Record<string, unknown> | undefined)?.n || 0);",
    "    const milestones = Number((mr[0] as Record<string, unknown> | undefined)?.n || 0);",
    "    const checkins = Number((cr[0] as Record<string, unknown> | undefined)?.n || 0);",
    "    const goals = Number((gr[0] as Record<string, unknown> | undefined)?.n || 0);",
    "",
    "    let score = 0;",
    "    if (journals >= 1) score += 20;",
    "    if (journals >= 3) score += 10;",
    "    if (milestones >= 1) score += 20;",
    "    if (checkins >= 1) score += 15;",
    "    if (goals >= 1) score += 15;",
