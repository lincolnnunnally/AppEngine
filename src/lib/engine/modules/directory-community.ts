function directoryDetailApiFile(): GeneratedModuleFile {
  return file("src/app/api/directory/[id]/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessCustomerArea } from "@/lib/auth/roles";',
    'import { getPerson, directoryEnabled } from "@/lib/db/directory";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "export async function GET(_request: Request, { params }: { params: Promise<{ id: string }> }) {",
    '  if (!directoryEnabled()) return NextResponse.json({ ok: false }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role)) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const { id } = await params;",
    "  const person = await getPerson(id);",
    '  if (!person) return NextResponse.json({ ok: false, message: "Not found." }, { status: 404 });',
    "  return NextResponse.json({ ok: true, person });",
    "}"
  ]);
}

// ---- src/app/directory/page.tsx (list + search) -----------------------------

function directoryPageFile(): GeneratedModuleFile {
  return file("src/app/directory/page.tsx", [
    'import Link from "next/link";',
    'import { requireCustomerAccess } from "@/lib/auth/session";',
    'import { listDirectory, fullName, directoryEnabled } from "@/lib/db/directory";',
    "",
    'export const dynamic = "force-dynamic";',
    "",
    "export default async function DirectoryPage({",
    "  searchParams",
    "}: {",
    "  searchParams?: Promise<{ q?: string }>;",
    "}) {",
    "  if (!directoryEnabled()) {",
    '    return (<main className="shell"><h1>Directory</h1><p>The directory is turned off for this app.</p></main>);',
    "  }",
    '  await requireCustomerAccess("/directory");',
    "  const params = searchParams ? await searchParams : undefined;",
    '  const q = (params?.q || "").trim();',
    "  const people = await listDirectory({ q: q || undefined });",
    "",
    "  return (",
    '    <main className="shell">',
    '      <p className="eyebrow">Directory</p>',
    "      <h1>People</h1>",
    "      <p>Browse and search everyone in the directory.</p>",
    '      <form className="stack" method="get" role="search">',
    '        <input className="input" type="search" name="q" defaultValue={q} placeholder="Search by name or email" aria-label="Search the directory" />',
    '        <button className="button primary" type="submit">Search</button>',
    "      </form>",
    "      {people.length === 0 ? (",
    '        <p className="note">{q ? `No one matches "${q}" yet.` : "No one is in the directory yet."}</p>',
    "      ) : (",
    '        <section className="panel-list">',
    "          {people.map((person) => (",
    '            <Link className="wide-card" key={person.id} href={`/directory/${person.id}`}>',
    "              <span>{person.title || \"Member\"}</span>",
    "              <strong>{fullName(person)}</strong>",
    "              <p>{[person.email, [person.city, person.state].filter(Boolean).join(\", \")].filter(Boolean).join(\" · \")}</p>",
    "            </Link>",
    "          ))}",
    "        </section>",
    "      )}",
    "    </main>",
    "  );",
    "}"
  ]);
}

// ---- src/app/directory/[id]/page.tsx (profile) ------------------------------

function directoryProfilePageFile(): GeneratedModuleFile {
  return file("src/app/directory/[id]/page.tsx", [
    'import Link from "next/link";',
    'import { notFound } from "next/navigation";',
    'import { requireCustomerAccess } from "@/lib/auth/session";',
    'import { getPerson, fullName, directoryEnabled } from "@/lib/db/directory";',
    "",
    'export const dynamic = "force-dynamic";',
    "",
    "export default async function DirectoryProfilePage({ params }: { params: Promise<{ id: string }> }) {",
    "  if (!directoryEnabled()) {",
    '    return (<main className="shell"><h1>Directory</h1><p>The directory is turned off for this app.</p></main>);',
    "  }",
    '  await requireCustomerAccess("/directory");',
    "  const { id } = await params;",
    "  const person = await getPerson(id);",
    "  if (!person) notFound();",
    "",
    "  return (",
    '    <main className="shell">',
    '      <p className="eyebrow">Directory</p>',
    "      <h1>{fullName(person)}</h1>",
    "      {person.title ? <p className=\"note\">{person.title}</p> : null}",
    '      <section className="grid">',
    "        {person.email ? (<article className=\"card\"><span>Email</span><strong>{person.email}</strong></article>) : null}",
    "        {person.phone ? (<article className=\"card\"><span>Phone</span><strong>{person.phone}</strong></article>) : null}",
    "        {(person.city || person.state) ? (<article className=\"card\"><span>Location</span><strong>{[person.city, person.state].filter(Boolean).join(\", \")}</strong></article>) : null}",
    "      </section>",
    "      {person.bio ? <p>{person.bio}</p> : null}",
    '      <p><Link className="button" href="/directory">Back to directory</Link></p>',
    "    </main>",
    "  );",
    "}"
  ]);
}

