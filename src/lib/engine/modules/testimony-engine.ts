// ---- src/app/api/testimonies/route.ts (list + submit) -----------------------

function testimonyApiFile(): GeneratedModuleFile {
  return file("src/app/api/testimonies/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessCustomerArea } from "@/lib/auth/roles";',
    'import { listTestimonies, submitTestimony, testimonyEnabled } from "@/lib/db/testimony";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "export async function GET(request: Request) {",
    '  if (!testimonyEnabled()) return NextResponse.json({ ok: false, message: "Testimonies are turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role)) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const url = new URL(request.url);",
    "  const testimonies = await listTestimonies({",
    '    category: url.searchParams.get("category") || undefined,',
    '    search: url.searchParams.get("search") || undefined,',
    "    // Customers only ever see approved, public stories through this endpoint.",
    "    isApproved: true,",
    "    isPublic: true",
    "  });",
    "  return NextResponse.json({ ok: true, testimonies });",
    "}",
    "",
    "export async function POST(request: Request) {",
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role)) return NextResponse.json({ ok: false, message: "Please sign in to share your story." }, { status: 401 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const title = typeof body.title === "string" ? body.title.trim() : "";',
    '  const fullStory = typeof body.fullStory === "string" ? body.fullStory.trim() : "";',
    "  if (title.length < 2 || fullStory.length < 10) {",
    '    return NextResponse.json({ ok: false, message: "A title and a few sentences of your story are required." }, { status: 400 });',
    "  }",
    "  const tags = Array.isArray(body.tags) ? body.tags.map((t) => String(t).trim()).filter(Boolean) : [];",
    "  const created = await submitTestimony({",
    "    authorId: user?.id ? String(user.id) : null,",
    '    authorName: typeof body.authorName === "string" ? body.authorName : (user?.name || ""),',
    "    authorEmail: user?.email ? String(user.email) : undefined,",
    "    title,",
    "    fullStory,",
    '    summary: typeof body.summary === "string" ? body.summary : undefined,',
    '    videoUrl: typeof body.videoUrl === "string" ? body.videoUrl : undefined,',
    '    category: typeof body.category === "string" ? body.category : undefined,',
    "    tags,",
    "    isPublic: body.isPublic !== false,",
    "    isAnonymous: body.isAnonymous === true",
    "  });",
    '  if (!created) return NextResponse.json({ ok: false, message: "Connect a database to save stories." }, { status: 503 });',
    "  // Ported from create_testimony: stories are captured for review, not published live.",
    '  return NextResponse.json({ ok: true, testimony: created, message: "Thank you. Your story is in review and will be published once approved." });',
    "}"
  ]);
}

