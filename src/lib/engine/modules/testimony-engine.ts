// testimony-engine — capture, store, surface, and review real stories, then close
// the loop from a solved problem back to a testimony. Ported from ChurchConnect's
// real testimony system (backend/routes/testimonies.py):
//   - the testimony model (id, member_name, member_email, title, summary,
//     full_story, video_url + extract_youtube_id/thumbnail derivation, category,
//     tags, is_approved [defaults FALSE — requires review], is_public,
//     is_anonymous, is_featured, view_count, like_count, created_at,
//     follow_up_scheduled).
//   - GET /api/testimonies filtering (category, is_approved, is_public, search
//     across title/summary/tags), newest-first.
//   - GET /api/testimonies/featured and /matching/{category} (others-found-hope).
//   - POST /api/testimonies create_testimony (defaults is_approved=false, derives
//     youtube id + hqdefault thumbnail, schedules a 30-day follow-up when an email
//     is given and the story isn't anonymous — schedule_testimony_follow_up).
//   - POST /api/testimonies/{id}/view and /like counter bumps.
//   - the admin review/approval queue (is_approved gate) that solicit + approve
//     flows hang off of.
// Generalized off the church framing into plain, warm copy so it drops into any
// app. Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_TESTIMONY); customers read approved stories + submit,
// admins review/approve/feature.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

