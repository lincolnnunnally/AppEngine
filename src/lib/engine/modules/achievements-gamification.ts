    "        run += 1;",
    "      } else {",
    "        run = 1;",
    "      }",
    "      if (run > longest) longest = run;",
    "      prev = d;",
    "    }",
    "    return {",
    "      current,",
    "      longest: Math.max(longest, current),",
    "      paused,",
    "      pauseReason: metricRows[0]?.pause_reason ? String(metricRows[0].pause_reason) : \"\",",
    "      daysLeft",
    "    };",
    "  } catch {",
    "    return empty;",
    "  }",
    "}",
