// achievements-gamification — badges, achievements, points, streaks, and a
// leaderboard that reinforce momentum. Ported from RebuildingDads' real
// achievement system and Kindred-Connection's real streak system:
//   - achievements + user_achievements + user_growth_metrics tables
//     (supabase/migrations/..._add_moderation_and_achievements_tables.sql):
//     achievement (id, name, description, category, icon, criteria, points) and
//     the 11 seeded defaults (First Steps 10 … Growth Champion 100); the
//     user_achievements join (unlocked_at, celebrated, UNIQUE(user,achievement));
//     user_growth_metrics (assessments_completed, profile_updates, logins_count,
//     messages_sent, connections_made, positive_feedback_received,
//     helpful_interactions, growth_score, friendliness_score) with the
//     growth_score-desc leaderboard index.
//   - src/hooks/useAchievements.ts: loadAchievements (order by points asc),
//     loadUserAchievements + totalPoints reduce (sum of achievement.points),
//     checkAndAwardAchievement (skip if already unlocked, insert celebrated=false),
//     and updateGrowthMetrics threshold rules that award named achievements
//     (First Steps on first assessment, Social Butterfly on first message,
//     Connector at 5 / Super Connector at 10 connections, Profile Perfectionist
//     at 10 profile updates).
//   - src/components/AchievementsBadge.tsx: unlocked-vs-locked card grid, per
//     category color/icon, "X / Y Unlocked" + percent-complete summary.
//   - Kindred-Connection backend/routers/streak.py (pause/resume, days 1..21,
//     pause_until/pause_reason/pause_started_at) + becoming.py streak loop
//     (count consecutive qualifying days newest-first, skip paused days, break on
//     the first miss) — ported as a check-in streak with current + longest.
// Generalized off the church/dating framing into plain copy so it drops into any
// app. Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_ACHIEVEMENTS); customers see their own badges +
// streak + the leaderboard, admins define achievements.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

