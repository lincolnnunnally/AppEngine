// knowledge-base — a community troubleshooting knowledge base: categories of
// problems, a symptom-driven troubleshooting wizard, and step-by-step solution
// guides with the tools, parts/resources, and buy links to fix each one. Ported
// from the JeepFix community Q&A app's real Supabase model + component logic:
//   - CategoryPage.tsx      — categories, and each category's problems sorted by
//     frequency_score / severity / newest, with a severity filter (SEVERITY_ORDER).
//   - ProblemCard.tsx       — the problem summary card (title, description,
//     severity, "reported by N% of owners").
//   - ProblemDetailPage.tsx — a problem's solutions ordered by success_rate, each
//     with difficulty, estimated_time/estimated_cost, tools_needed, parts_needed,
//     resources (youtube/forum/parts/article) and getYouTubeVideoId embedding.
//   - SolutionPartsList.tsx — a solution's parts list (part_name, part_number,
//     amazon_url + affiliate-tag rewrite, estimated_price, is_required, notes,
//     order_index), split into required vs optional with a total cost.
//   - TroubleshootingWizard.tsx — pick symptoms, match against problem_symptoms
//     (relevance_score), rank problems by matched/total symptom coverage then
//     score, return the top 5 with a match percentage.
// Generalized off the Jeep framing into plain copy so it drops into any app.
// Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_KB). Customers browse categories/problems/solutions
// and run the wizard; admins report problems + add solutions/parts.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

