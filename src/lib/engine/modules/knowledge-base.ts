function kbWizardClientFile(): GeneratedModuleFile {
  return file("src/app/knowledge-base/wizard/wizard-client.tsx", [
    '"use client";',
    "",
    'import { useState } from "react";',
    'import Link from "next/link";',
    "",
    "type Symptom = { id: string; description: string };",
    "type ProblemMatch = {",
    "  problem: { id: string; title: string; severity: string; categoryName: string };",
    "  matchScore: number;",
    "  matchedSymptoms: number;",
    "  totalSymptoms: number;",
    "  matchPercentage: number;",
    "};",
    "",
    "// Ported from TroubleshootingWizard.tsx: pick symptoms, POST them to the matcher,",
    "// render the ranked results with match percentages. Scoring runs server-side.",
    "export function TroubleshootingWizard({ symptoms }: { symptoms: Symptom[] }) {",
    "  const [selected, setSelected] = useState<Set<string>>(new Set());",
    "  const [matches, setMatches] = useState<ProblemMatch[]>([]);",
    '  const [filter, setFilter] = useState("");',
    "  const [searching, setSearching] = useState(false);",
    "",
    "  async function runMatch(ids: Set<string>) {",
    "    if (ids.size === 0) {",
    "      setMatches([]);",
    "      return;",
    "    }",
    "    setSearching(true);",
    "    try {",
    '      const response = await fetch("/api/knowledge-base/wizard", {',
    '        method: "POST",',
    '        headers: { "Content-Type": "application/json" },',
    "        body: JSON.stringify({ symptomIds: Array.from(ids) })",
    "      });",
    "      const data = await response.json();",
    "      setMatches(data.ok ? data.matches : []);",
    "    } catch {",
    "      setMatches([]);",
    "    } finally {",
    "      setSearching(false);",
    "    }",
    "  }",
    "",
    "  function toggle(id: string) {",
    "    const next = new Set(selected);",
    "    if (next.has(id)) next.delete(id);",
    "    else next.add(id);",
    "    setSelected(next);",
    "    runMatch(next);",
    "  }",
    "",
    "  function clearAll() {",
    "    setSelected(new Set());",
    "    setMatches([]);",
    "  }",
    "",
    "  const lower = filter.trim().toLowerCase();",
    "  const visible = lower ? symptoms.filter((s) => s.description.toLowerCase().includes(lower)) : symptoms;",
    "",
    "  return (",
    '    <div className="grid-two">',
    "      <section>",
    "        <h2>Select your symptoms ({selected.size} selected)",
    '          {selected.size > 0 ? <button className="button" type="button" onClick={clearAll}>Clear all</button> : null}',
    "        </h2>",
    "        {symptoms.length > 8 ? (",
    '          <input className="input" type="search" value={filter} onChange={(e) => setFilter(e.target.value)} placeholder="Filter symptoms" aria-label="Filter symptoms" />',
    "        ) : null}",
    '        <ul className="panel-list">',
    "          {visible.map((s) => {",
    "            const isSelected = selected.has(s.id);",
    "            return (",
    "              <li key={s.id}>",
    '                <button className={isSelected ? "button primary" : "button"} type="button" role="checkbox" aria-checked={isSelected} onClick={() => toggle(s.id)}>',
    "                  {isSelected ? \"\\u2713 \" : \"\"}{s.description}",
    "                </button>",
    "              </li>",
    "            );",
    "          })}",
    "        </ul>",
    "        {visible.length === 0 && filter ? <p className=\"note\">No symptoms match &quot;{filter}&quot;</p> : null}",
    "      </section>",
    "      <section aria-live=\"polite\">",
    "        <h2>Possible problems</h2>",
    "        {selected.size === 0 ? (",
    '          <p className="note">Select symptoms to see possible problems.</p>',
    "        ) : searching ? (",
    '          <p className="note">Searching…</p>',
    "        ) : matches.length === 0 ? (",
    '          <p className="note">No matches found. Try selecting different symptoms.</p>',
    "        ) : (",
    "          matches.map((m) => (",
    '            <Link className="wide-card" key={m.problem.id} href={`/knowledge-base/problem/${m.problem.id}`}>',
    "              <strong>{m.problem.title}</strong>",
    '              <p className="note">{m.matchPercentage}% match · {m.matchedSymptoms}/{m.totalSymptoms} symptoms</p>',
    '              <span className={`badge severity-${m.problem.severity}`}>{m.problem.severity}</span>',
    '              {m.problem.categoryName ? <span className="note"> {m.problem.categoryName}</span> : null}',
    "            </Link>",
    "          ))",
    "        )}",
    "      </section>",
    "    </div>",
    "  );",
    "}"
  ]);
}

