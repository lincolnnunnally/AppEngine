// volunteer-safety — volunteer scheduling plus background checks and waivers for
// safe service, ported from ChurchConnect's real volunteer-safety system:
//   - src/components/VolunteerAvailability.tsx: the availability profile — an
//     "available to volunteer" toggle, a scope (my_org | any_org, from
//     my_church|any_church), the fixed VOLUNTEER_SKILLS list, and free-text
//     availability notes; saved via POST /api/volunteers/toggle.
//   - backend/routes/background_checks.py: the background-check model (person,
//     role, check_type, status pending|in_progress|cleared|flagged|expired,
//     consent_given/consent_date required-to-initiate, initiated_at, 2-year
//     expires_at, result_summary, notes[]); the BACKGROUND_CHECK_REQUIRED_ROLES
//     list + /required-for-role substring match; status/expiry recompute in
//     get_background_check_status; and get_required_trainings_for_role's
//     role→training mapping (volunteer-orientation for everyone, child-safety +
//     mandated-reporter for kids roles, etc.) with the can_serve gate.
//   - src/components/WaiverManagement.tsx + the event_waivers / waiver_signatures
//     migration: reusable waiver templates (title, waiver_text, is_default) and
//     on-site signatures (full_name, email, phone, signature_data, a waiver_text
//     snapshot at time of signing, signed_at) with the agree-to-waiver gate and
//     the signed-count roll-up.
// Generalized off the church framing ("church"→"organization", "my_church"→
// "my_org") into plain copy so it drops into any app. Runs on the app owner's own
// database with graceful sample-data fallback, feature-flagged (FEATURE_VOLUNTEER);
// customers manage their own availability + sign waivers, admins run checks +
// review the roster, own the waiver templates, and clear people to serve.

