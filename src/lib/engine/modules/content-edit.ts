    '        beforeWeight: el.getAttribute("data-ce-weight") || "",',
    '        beforeColor: el.getAttribute("data-ce-color") || ""',
    "      };",
    '      el.setAttribute("data-ce-selected", "true");',
    '      el.setAttribute("contenteditable", "plaintext-only");',
    "      // Browsers without plaintext-only fall back to a plain contenteditable;",
    "      // saves still persist plain text because we read textContent.",
    '      if (!el.isContentEditable) el.setAttribute("contenteditable", "true");',
    "      const rect = el.getBoundingClientRect();",
    "      setToolbarPos({",
    "        top: Math.min(Math.max(8, rect.bottom + 10), Math.max(8, window.innerHeight - 200)),",
    "        left: Math.min(Math.max(8, rect.left), Math.max(8, window.innerWidth - 296))",
    "      });",
    "      setDraft({ size: next.beforeSize, weight: next.beforeWeight, color: next.beforeColor });",
    "      setSelected(next);",
    '      setStatus("idle");',
    '      setMessage("");',
    "      el.focus();",
    "    },",
    "    [clearSelection]",
    "  );",
    "",
    "  const save = useCallback(async () => {",
    "    const current = selectedRef.current;",
    "    if (!current) return;",
    "    const original = originalsRef.current.get(current.path) ?? current.beforeText;",
    '    const text = current.el.textContent || "";',
    '    const size = current.el.getAttribute("data-ce-size");',
    '    const weight = current.el.getAttribute("data-ce-weight");',
    '    const color = current.el.getAttribute("data-ce-color");',
    '    const page = pathname || "/";',
    '    setStatus("saving");',
    '    setMessage("");',
    "    try {",
    "      if (text === original && !size && !weight && !color) {",
    "        // Everything matches the original again — drop the stored row.",
    '        const res = await fetch("/api/content-overrides", {',
    '          method: "DELETE",',
    '          headers: { "Content-Type": "application/json" },',
    "          body: JSON.stringify({ page, elementPath: current.path })",
    "        });",
    '        if (!res.ok) throw new Error("delete failed");',
    "        overridesRef.current.delete(current.path);",
    "      } else {",
    '        const res = await fetch("/api/content-overrides", {',
    '          method: "POST",',
    '          headers: { "Content-Type": "application/json" },',
    "          body: JSON.stringify({",
    "            page,",
    "            elementPath: current.path,",
    "            text: text === original ? null : text,",
    "            size,",
    "            weight,",
    "            color,",
    "            originalText: original",
    "          })",
    "        });",
    "        const data = (await res.json().catch(() => null)) as { ok?: boolean; override?: Override } | null;",
    '        if (!res.ok || !data || data.ok !== true || !data.override) throw new Error("save failed");',
    "        overridesRef.current.set(current.path, data.override);",
    "      }",
    '      current.el.removeAttribute("contenteditable");',
    '      current.el.removeAttribute("data-ce-selected");',
    "      setSelected(null);",
    '      setStatus("saved");',
    '      setMessage("Saved. Everyone sees this now.");',
    "    } catch {",
    '      setStatus("error");',
    '      setMessage("Could not save. Please try again.");',
    "    }",
    "  }, [pathname]);",
    "",
    "  // Restore the true original (pre-any-override) and remove the stored row.",
    "  const reset = useCallback(async () => {",
    "    const current = selectedRef.current;",
    "    if (!current) return;",
    "    const original = originalsRef.current.get(current.path) ?? current.beforeText;",
    '    setStatus("saving");',
    "    try {",
    '      const res = await fetch("/api/content-overrides", {',
    '        method: "DELETE",',
    '        headers: { "Content-Type": "application/json" },',
