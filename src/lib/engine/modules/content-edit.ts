// content-edit — Divi-style visual content editing (Phase 1), owner-requested
// 2026-07-10: "occasions where it would have been easier to click a text and
// change it than telling the agent what I am talking about."
//
// What it does: an owner/admin gets a floating "Edit page" toggle on every page.
// With it on, clicking any text element makes it editable in place; a small
// toolbar offers size (S/M/L/XL), weight, and color choices drawn from the app's
// design tokens (CSS variables) — never arbitrary CSS. Saving persists the edit
// to a content_overrides row keyed by (page, stable element path), and the same
// runtime applies stored overrides for EVERY visitor at render, so an edit made
// once shows for everyone. Anonymous visitors get zero edit affordances: the
// server decides canEdit from the session role, and the toggle/toolbar only
// render after that answer comes back true.
//
// How it mounts: the module emits src/app/template.tsx — the base generator does
// not emit one, so the root template is a collision-free hook that wraps every
// route (inside the root layout) without touching any base file. The runtime is
// a client component that (a) fetches this page's overrides + canEdit, (b)
// applies overrides to the DOM by resolving each stored element path, and (c)
// for editors only, layers on the click-to-edit interaction. Because templates
// remount per navigation, Edit mode is per-page and overrides re-apply on every
// route change.
//
// Stable element paths: a chain of tag:nth-of-type(i) segments from <body> down
// (e.g. "main:nth-of-type(1)>h1:nth-of-type(1)"). Type-position survives text
// changes and unrelated attribute churn; it does move if the page's structure is
// reordered — the /admin/content page lists every stored edit so a stranded one
// can be removed. Phase 2 (drag-reorder, spacing) extends this same schema; see
// docs/content-edit-phase-2-notes.md in the factory repo.
//
// Styling tokens, not CSS: the toolbar writes data-ce-size / data-ce-weight /
// data-ce-color attributes; a stylesheet the runtime ships maps those to the
// generated theme's CSS variables (--ink/--muted/--accent), with --ce-* override
// hooks so a ported/retrofitted app can supply its own palette without --accent
// existing. Text is written with textContent (never innerHTML) so an edit can
// never inject markup. Mixed-content elements (a <p> holding a <strong>) edit as
// flattened plain text — shown live in the element before saving, so what the
// owner sees is exactly what persists.
//
// Guards: reads are public on purpose (overrides ARE page content); writes and
// deletes require canAccessAdmin (owner/admin) server-side. Feature-flagged
// (FEATURE_CONTENT_EDIT); with no database the runtime stays silent and the API
// answers honestly instead of pretending to save.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

