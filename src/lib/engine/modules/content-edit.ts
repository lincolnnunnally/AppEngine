    "  }, []);",
    "",
    "  const toggleEditMode = useCallback(() => {",
    "    if (editMode) clearSelection(true);",
    "    setEditMode(!editMode);",
    '    setStatus("idle");',
    '    setMessage("");',
    "  }, [editMode, clearSelection]);",
    "",
    "  // Edit-mode interaction: capture-phase listeners so clicks select text for",
    "  // editing instead of following links or pressing buttons. Clicks inside the",
    "  // editor UI or the element being edited pass through untouched.",
    "  useEffect(() => {",
    "    if (!editMode) return;",
    "    const onClick = (event: MouseEvent) => {",
    "      const target = event.target;",
    "      if (!(target instanceof Element)) return;",
    '      if (target.closest("[data-ce-ui]")) return;',
    "      const current = selectedRef.current;",
