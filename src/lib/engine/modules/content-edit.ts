// ---- src/app/api/content-overrides/route.ts (read public, write admin) ------

function overridesApiFile(): GeneratedModuleFile {
  return file("src/app/api/content-overrides/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessAdmin } from "@/lib/auth/roles";',
    "import {",
    "  MAX_TEXT_LENGTH,",
    "  OVERRIDE_COLORS,",
    "  OVERRIDE_SIZES,",
    "  OVERRIDE_WEIGHTS,",
    "  contentEditEnabled,",
    "  deleteOverride,",
    "  isValidElementPath,",
    "  listOverrides,",
    "  normalizePage,",
    "  upsertOverride",
    '} from "@/lib/db/content-overrides";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    'const NO_STORE = { "Cache-Control": "no-store" };',
    "",
    "// Reading is public on purpose: stored overrides ARE the page's content, so",
    "// every visitor needs them to render what the owner published. canEdit is",
    "// decided HERE from the server session role — the client never asserts it.",
    "export async function GET(request: Request) {",
    "  if (!contentEditEnabled()) {",
    "    return NextResponse.json({ ok: true, enabled: false, canEdit: false, overrides: [] }, { headers: NO_STORE });",
    "  }",
    "  const url = new URL(request.url);",
    '  const page = normalizePage(url.searchParams.get("page") || "");',
    '  if (!page) return NextResponse.json({ ok: false, message: "A page path is required." }, { status: 400 });',
    "  const user = await getCurrentUser();",
    "  const overrides = await listOverrides(page);",
    "  return NextResponse.json(",
    "    { ok: true, enabled: true, canEdit: canAccessAdmin(user?.role), overrides },",
    "    { headers: NO_STORE }",
    "  );",
    "}",
    "",
    "function tokenOrNull(value: unknown, allowed: string[]): string | null {",
    '  return typeof value === "string" && allowed.includes(value) ? value : null;',
    "}",
    "",
    "// Save an edit — owners and admins only, enforced on the server.",
    "export async function POST(request: Request) {",
    '  if (!contentEditEnabled()) return NextResponse.json({ ok: false, message: "Visual editing is turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!user || !canAccessAdmin(user.role)) return NextResponse.json({ ok: false, message: "Owners and admins only." }, { status: 403 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const page = normalizePage(typeof body.page === "string" ? body.page : "");',
    '  if (!page) return NextResponse.json({ ok: false, message: "A page path is required." }, { status: 400 });',
    '  const elementPath = typeof body.elementPath === "string" ? body.elementPath : "";',
    '  if (!isValidElementPath(elementPath)) return NextResponse.json({ ok: false, message: "That element cannot be edited." }, { status: 400 });',
    '  const text = typeof body.text === "string" ? body.text.slice(0, MAX_TEXT_LENGTH) : null;',
    "  const size = tokenOrNull(body.size, OVERRIDE_SIZES);",
    "  const weight = tokenOrNull(body.weight, OVERRIDE_WEIGHTS);",
    "  const color = tokenOrNull(body.color, OVERRIDE_COLORS);",
    "  if (text == null && !size && !weight && !color) {",
    '    return NextResponse.json({ ok: false, message: "Nothing changed — the text matches the original and no style is set." }, { status: 400 });',
    "  }",
    '  const originalText = typeof body.originalText === "string" ? body.originalText.slice(0, MAX_TEXT_LENGTH) : "";',
    "  const override = await upsertOverride({",
    "    page,",
    "    elementPath,",
    "    text,",
    "    size,",
    "    weight,",
    "    color,",
    "    originalText,",
    "    updatedBy: user.id ? String(user.id) : null",
    "  });",
    '  if (!override) return NextResponse.json({ ok: false, message: "Connect a database to keep edits." }, { status: 503 });',
    "  return NextResponse.json({ ok: true, override });",
    "}",
    "",
    "// Remove an edit (restore the original) — owners and admins only.",
    "export async function DELETE(request: Request) {",
    '  if (!contentEditEnabled()) return NextResponse.json({ ok: false, message: "Visual editing is turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!user || !canAccessAdmin(user.role)) return NextResponse.json({ ok: false, message: "Owners and admins only." }, { status: 403 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const page = normalizePage(typeof body.page === "string" ? body.page : "");',
    '  const elementPath = typeof body.elementPath === "string" ? body.elementPath : "";',
    "  if (!page || !isValidElementPath(elementPath)) {",
    '    return NextResponse.json({ ok: false, message: "A page path and element path are required." }, { status: 400 });',
    "  }",
    "  const removed = await deleteOverride(page, elementPath);",
    '  if (!removed) return NextResponse.json({ ok: false, message: "Connect a database to manage edits." }, { status: 503 });',
    "  return NextResponse.json({ ok: true });",
    "}"
  ]);
}

