    "          {message ? (",
    '            <p className={status === "error" ? "ce-status ce-error" : "ce-status"} role="status">{message}</p>',
    "          ) : null}",
    "        </div>",
    "      ) : null}",
    "    </>",
    "  );",
    "}"
