    "",
    "// Editable = the element itself holds visible text (a direct non-empty text",
    "// node), not just text somewhere in its subtree — that keeps the click target",
    "// on the deepest element that owns the words.",
    "function hasOwnText(el: Element): boolean {",
    "  if (SKIP_TAGS.has(el.tagName)) return false;",
    "  return Array.from(el.childNodes).some(",
    '    (child) => child.nodeType === Node.TEXT_NODE && (child.textContent || "").trim().length > 0',
    "  );",
    "}",
    "",
    "function editableFrom(start: Element): HTMLElement | null {",
    '  if (start.closest("[data-ce-ui]")) return null;',
    "  let node: Element | null = start;",
    "  while (node && node !== document.body) {",
    "    if (node instanceof HTMLElement && hasOwnText(node)) return node;",
    "    node = node.parentElement;",
    "  }",
    "  return null;",
    "}",
    "",
    "function syncAttr(el: Element, name: string, value: string | null) {",
    "  if (value) {",
    "    if (el.getAttribute(name) !== value) el.setAttribute(name, value);",
    "  } else if (el.hasAttribute(name)) {",
    "    el.removeAttribute(name);",
    "  }",
    "}",
    "",
    "// Size tokens scale the element's OWN rendered size (an em would scale the",
    '// parent\'s — "XL" on a large heading must grow it, not shrink it). Measure',
    "// the pre-token size once and expose it to the CSS calc() via an inline var.",
    "function ensureSizeBase(el: HTMLElement) {",
    '  if (el.style.getPropertyValue("--ce-size-base")) return;',
    '  const token = el.getAttribute("data-ce-size");',
    '  if (token) el.removeAttribute("data-ce-size");',
    '  el.style.setProperty("--ce-size-base", getComputedStyle(el).fontSize);',
    '  if (token) el.setAttribute("data-ce-size", token);',
    "}",
    "",
    "// Apply a size token (or clear it) with its measured base kept in step.",
    "function syncSize(el: HTMLElement, value: string | null) {",
    "  if (value) ensureSizeBase(el);",
    '  syncAttr(el, "data-ce-size", value);',
    '  if (!value) el.style.removeProperty("--ce-size-base");',
    "}",
    "",
    "export function ContentEditRuntime() {",
    "  const pathname = usePathname();",
    "  const [canEdit, setCanEdit] = useState(false);",
    "  const [editMode, setEditMode] = useState(false);",
    "  const [selected, setSelected] = useState<Selected | null>(null);",
    '  const [draft, setDraft] = useState({ size: "", weight: "", color: "" });',
    '  const [status, setStatus] = useState<"idle" | "saving" | "saved" | "error">("idle");',
    '  const [message, setMessage] = useState("");',
    "  const [toolbarPos, setToolbarPos] = useState({ top: 8, left: 8 });",
    "  const overridesRef = useRef<Map<string, Override>>(new Map());",
    "  // Original text per path, captured BEFORE any override or edit touches the",
    "  // element — reset restores this, and the first save records it server-side.",
    "  const originalsRef = useRef<Map<string, string>>(new Map());",
    "  const selectedRef = useRef<Selected | null>(null);",
    "",
    "  useEffect(() => {",
    "    selectedRef.current = selected;",
    "  }, [selected]);",
    "",
    "  const applyAll = useCallback(() => {",
    "    overridesRef.current.forEach((override) => {",
    "      const el = resolvePath(override.elementPath);",
    '      if (!el || el.closest("[data-ce-ui]")) return;',
    "      if (!originalsRef.current.has(override.elementPath)) {",
    '        originalsRef.current.set(override.elementPath, el.textContent || "");',
    "      }",
    "      // Never touch the element mid-edit — the live draft wins until saved.",
    "      if (selectedRef.current && selectedRef.current.el === el) return;",
    "      if (override.text != null && el.textContent !== override.text) el.textContent = override.text;",
    "      syncSize(el, override.size);",
    '      syncAttr(el, "data-ce-weight", override.weight);',
    '      syncAttr(el, "data-ce-color", override.color);',
    "    });",
    "  }, []);",
    "",
    "  // Fetch this page's overrides + the server's canEdit verdict, then apply.",
    "  // A few delayed re-applies catch content that hydrates in after us.",
    "  useEffect(() => {",
    "    let cancelled = false;",
    "    const timers: Array<ReturnType<typeof setTimeout>> = [];",
    '    fetch("/api/content-overrides?page=" + encodeURIComponent(pathname || "/"), { cache: "no-store" })',
    "      .then((res) => (res.ok ? res.json() : null))",
    "      .then((data) => {",
    "        if (cancelled || !data || data.ok !== true || data.enabled !== true) return;",
    "        setCanEdit(data.canEdit === true);",
    "        const map = new Map<string, Override>();",
    "        (Array.isArray(data.overrides) ? data.overrides : []).forEach((entry: Override) => {",
    "          if (entry && typeof entry.elementPath === \"string\") map.set(entry.elementPath, entry);",
    "        });",
    "        overridesRef.current = map;",
    "        applyAll();",
    "        [150, 400, 1000, 2200].forEach((delay) => timers.push(setTimeout(applyAll, delay)));",
    "      })",
    "      .catch(() => {",
    "        // The page must render fine even if the overrides fetch fails.",
    "      });",
    "    return () => {",
    "      cancelled = true;",
    "      timers.forEach(clearTimeout);",
    "    };",
    "  }, [pathname, applyAll]);",
    "",
    "  useEffect(() => {",
    '    document.documentElement.classList.toggle("ce-editing", editMode);',
    '    return () => document.documentElement.classList.remove("ce-editing");',
    "  }, [editMode]);",
    "",
    "  // Deselect, optionally restoring the element to how it looked beforehand.",
    "  const clearSelection = useCallback((restore: boolean) => {",
    "    const current = selectedRef.current;",
    "    if (!current) return;",
    '    current.el.removeAttribute("contenteditable");',
    '    current.el.removeAttribute("data-ce-selected");',
    "    if (restore) {",
    "      current.el.textContent = current.beforeText;",
    "      syncSize(current.el, current.beforeSize || null);",
    '      syncAttr(current.el, "data-ce-weight", current.beforeWeight || null);',
    '      syncAttr(current.el, "data-ce-color", current.beforeColor || null);',
    "    }",
    "    setSelected(null);",
    '    setStatus("idle");',
    '    setMessage("");',
    "  }, []);",
    "",
    "  const selectElement = useCallback(",
    "    (el: HTMLElement) => {",
    "      const path = pathFor(el);",
    "      if (!path) {",
    '        setStatus("error");',
    '        setMessage("This element cannot be edited.");',
    "        return;",
    "      }",
    "      clearSelection(true);",
    "      if (!originalsRef.current.has(path)) {",
    '        originalsRef.current.set(path, el.textContent || "");',
