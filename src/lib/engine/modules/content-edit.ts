    "            onClick={toggleEditMode}",
    "          >",
    '            {editMode ? "Done editing" : "Edit page"}',
    "          </button>",
    "          {editMode && !selected && !message ? (",
    '            <p className="ce-hint" role="status">Click any text to edit it.</p>',
    "          ) : null}",
    "          {selected ? (",
    "            <div",
    '              className="ce-toolbar"',
    '              role="toolbar"',
    '              aria-label="Text style"',
    "              style={{ top: toolbarPos.top, left: toolbarPos.left }}",
    "            >",
    '              <div className="ce-row">',
    "                <span>Size</span>",
    "                {SIZE_CHOICES.map(([value, label]) => (",
    "                  <button",
    "                    key={label}",
    '                    type="button"',
    "                    aria-pressed={draft.size === value}",
    '                    onClick={() => setStyle("size", value)}',
    "                  >",
    "                    {label}",
    "                  </button>",
    "                ))}",
    "              </div>",
    '              <div className="ce-row">',
    "                <span>Weight</span>",
    "                {WEIGHT_CHOICES.map(([value, label]) => (",
    "                  <button",
    "                    key={label}",
    '                    type="button"',
    "                    aria-pressed={draft.weight === value}",
    '                    onClick={() => setStyle("weight", value)}',
    "                  >",
    "                    {label}",
    "                  </button>",
    "                ))}",
    "              </div>",
    '              <div className="ce-row">',
    "                <span>Color</span>",
    "                {COLOR_CHOICES.map(([value, label]) => (",
    "                  <button",
    "                    key={label}",
    '                    type="button"',
    "                    aria-pressed={draft.color === value}",
    '                    onClick={() => setStyle("color", value)}',
    "                  >",
    "                    {label}",
    "                  </button>",
    "                ))}",
    "              </div>",
    '              <div className="ce-actions">',
    '                <button className="ce-save" type="button" onClick={() => void save()} disabled={status === "saving"}>',
