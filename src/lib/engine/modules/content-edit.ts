    "      });",
    '      if (!res.ok) throw new Error("delete failed");',
    "      overridesRef.current.delete(current.path);",
    "      current.el.textContent = original;",
    "      syncSize(current.el, null);",
    '      current.el.removeAttribute("data-ce-weight");',
    '      current.el.removeAttribute("data-ce-color");',
    '      current.el.removeAttribute("contenteditable");',
    '      current.el.removeAttribute("data-ce-selected");',
    "      setSelected(null);",
    '      setStatus("saved");',
    '      setMessage("Restored the original.");',
    "    } catch {",
    '      setStatus("error");',
    '      setMessage("Could not reset. Please try again.");',
    "    }",
    "  }, [pathname]);",
    "",
    "  // Style tokens preview live on the element itself, so what the owner sees",
    "  // while choosing is exactly what saving will persist.",
    '  const setStyle = useCallback((kind: "size" | "weight" | "color", value: string) => {',
    "    const current = selectedRef.current;",
    "    if (!current) return;",
    '    if (kind === "size") syncSize(current.el, value || null);',
    '    else syncAttr(current.el, "data-ce-" + kind, value || null);',
