function brandKitApiFile(): GeneratedModuleFile {
  return file("src/app/api/brand-kit/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessCustomerArea } from "@/lib/auth/roles";',
    'import { listProjects, createProjectWithConcepts, brandKitEnabled } from "@/lib/db/brand-kit";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "export async function GET() {",
    '  if (!brandKitEnabled()) return NextResponse.json({ ok: false, message: "The brand kit generator is turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!user || !canAccessCustomerArea(user.role)) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const projects = await listProjects(String(user.id));",
    "  return NextResponse.json({ ok: true, projects });",
    "}",
    "",
    "export async function POST(request: Request) {",
    '  if (!brandKitEnabled()) return NextResponse.json({ ok: false, message: "The brand kit generator is turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!user || !canAccessCustomerArea(user.role)) return NextResponse.json({ ok: false, message: "Please sign in to generate concepts." }, { status: 401 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const name = typeof body.name === "string" ? body.name.trim() : "";',
    '  const brandDescription = typeof body.brandDescription === "string" ? body.brandDescription.trim() : "";',
    "  if (name.length < 2) {",
    '    return NextResponse.json({ ok: false, message: "Give your brand a name (at least 2 characters)." }, { status: 400 });',
    "  }",
    "  if (brandDescription.length < 10) {",
    '    return NextResponse.json({ ok: false, message: "Describe your brand in a sentence or two so we can shape the concepts." }, { status: 400 });',
    "  }",
    "  const project = await createProjectWithConcepts({ ownerId: String(user.id), name, brandDescription });",
    '  if (!project) return NextResponse.json({ ok: false, message: "Connect a database to save your brand concepts." }, { status: 503 });',
    "  return NextResponse.json({ ok: true, project });",
    "}"
  ]);
}

