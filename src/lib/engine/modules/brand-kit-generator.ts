function deleteButtonFile(): GeneratedModuleFile {
  return file("src/app/brand-kit/[id]/delete-button.tsx", [
    '"use client";',
    "",
    'import { useState } from "react";',
    'import { useRouter } from "next/navigation";',
    "",
    "export function DeleteProjectButton({ id }: { id: string }) {",
    "  const router = useRouter();",
    '  const [status, setStatus] = useState<"idle" | "working" | "error">("idle");',
    '  const [message, setMessage] = useState<string>("");',
    "",
    "  async function remove() {",
    '    setStatus("working");',
    '    setMessage("");',
    "    try {",
    "      const response = await fetch(`/api/brand-kit/${id}`, { method: \"DELETE\" });",
    "      const data = await response.json();",
    "      if (!response.ok || !data.ok) {",
    '        setStatus("error");',
    '        setMessage(data.message || "Could not delete this project.");',
    "        return;",
    "      }",
    '      router.push("/brand-kit");',
    "      router.refresh();",
    "    } catch {",
    '      setStatus("error");',
    '      setMessage("Could not reach the server.");',
    "    }",
    "  }",
    "",
    "  return (",
    "    <>",
    '      <button className="button" type="button" onClick={remove} disabled={status === "working"}>',
    '        {status === "working" ? "Deleting…" : "Delete project"}',
    "      </button>",
    '      {status === "error" ? <span className="note" role="alert">{message}</span> : null}',
    "    </>",
    "  );",
    "}"
  ]);
}

