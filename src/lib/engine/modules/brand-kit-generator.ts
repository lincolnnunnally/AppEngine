// ---- src/app/brand-kit/brand-prompt-form.tsx (client) -----------------------
// Ported from Iconium src/app/page.tsx HomePage (prompt state + generate button).

function brandPromptFormFile(): GeneratedModuleFile {
  return file("src/app/brand-kit/brand-prompt-form.tsx", [
    '"use client";',
    "",
    'import { useState } from "react";',
    'import { useRouter } from "next/navigation";',
    "",
    "export function BrandPromptForm() {",
    "  const router = useRouter();",
    '  const [name, setName] = useState("");',
    '  const [prompt, setPrompt] = useState("");',
    '  const [status, setStatus] = useState<"idle" | "generating" | "error">("idle");',
    '  const [message, setMessage] = useState<string>("");',
    "",
    "  async function generate(event: React.FormEvent<HTMLFormElement>) {",
    "    event.preventDefault();",
    '    setStatus("generating");',
    '    setMessage("");',
    "    try {",
    '      const response = await fetch("/api/brand-kit", {',
    '        method: "POST",',
    '        headers: { "Content-Type": "application/json" },',
    "        body: JSON.stringify({ name, brandDescription: prompt })",
    "      });",
    "      const data = await response.json();",
    "      if (!response.ok || !data.ok) {",
    '        setStatus("error");',
    '        setMessage(data.message || "Something went wrong. Please try again.");',
    "        return;",
    "      }",
    "      router.push(`/brand-kit/${data.project.id}`);",
    "      router.refresh();",
    "    } catch {",
    '      setStatus("error");',
    '      setMessage("Could not reach the server. Please try again.");',
    "    }",
    "  }",
    "",
    "  return (",
    '    <form className="stack" onSubmit={generate}>',
    '      <input className="input" value={name} onChange={(e) => setName(e.target.value)} placeholder="Brand name" aria-label="Brand name" required />',
    '      <textarea className="input" value={prompt} onChange={(e) => setPrompt(e.target.value)} rows={5} placeholder="Describe your organization, audience, style, and symbolism…" aria-label="Brand description" required />',
    '      {status === "error" ? <p className="note" role="alert">{message}</p> : null}',
    '      <button className="button primary" type="submit" disabled={status === "generating"}>',
    '        {status === "generating" ? "Generating concepts…" : "Generate logo concepts"}',
    "      </button>",
    "    </form>",
    "  );",
    "}"
  ]);
}

