    "    mark,",
    '    `<text x="120" y="122" text-anchor="middle" dominant-baseline="middle" fill="${p.ink}" font-family="${p.font}" font-size="34" font-weight="700">${initials}</text>`,',
    '    `<text x="120" y="196" text-anchor="middle" fill="${p.muted}" font-family="${p.font}" font-size="16" letter-spacing="2">${label.toUpperCase()}</text>`,',
    '    "</svg>"',
    '  ].join("");',
    "}",
    "",
    "// A browser-safe data URI for the preview, stored in previewUrl (ported field).",
    "export function svgDataUri(svg: string): string {",
    '  const encoded = typeof Buffer !== "undefined"',
    '    ? Buffer.from(svg, "utf8").toString("base64")',
    "    : btoa(unescape(encodeURIComponent(svg)));",
    "  return `data:image/svg+xml;base64,${encoded}`;",
    "}",
    "",
    "// Expand a brand prompt into one concept per style direction — the core of",
    "// Iconium's 'Generate Logo Concepts'. Each concept gets its ported title +",
    "// description, a real SVG, a preview data URI, and a palette.",
    "export function generateConcepts(name: string, brandDescription: string): Omit<BrandLogo, \"id\" | \"projectId\" | \"createdAt\">[] {",
    "  const seed = (brandDescription || \"\").trim();",
    "  return STYLE_CONCEPTS.map((concept) => {",
    "    const svg = renderLogoSvg(name, concept.id);",
    "    const description = seed",
    "      ? `${concept.description} — shaped around: ${seed.slice(0, 120)}`",
    "      : concept.description;",
    "    return {",
    "      style: concept.id,",
    "      title: concept.title,",
    "      description,",
    "      svgData: svg,",
    "      previewUrl: svgDataUri(svg),",
    "      palette: paletteFor(concept.id)",
    "    };",
    "  });",
    "}",
    "",
    "// Optional AI expansion, ported in spirit from Iconium's openai dependency: if a",
    "// key is configured we could enrich the descriptions; when it is unset (the",
    "// common case) we degrade gracefully to the built-in generator above and never",
    "// block the UI.",
    "export function aiConfigured(): boolean {",
    "  return Boolean((process.env.BRAND_KIT_AI_KEY || \"\").trim());",
