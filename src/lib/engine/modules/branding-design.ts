    '        setStatus("error");',
    '        setMessage(data.message || "Could not save this design.");',
    "        return;",
    "      }",
    '      setStatus("done");',
    '      setMessage("Saved to your designs.");',
    "      setSelected(null);",
    "      router.refresh();",
    "    } catch {",
    '      setStatus("error");',
    '      setMessage("Could not reach the server. Please try again.");',
    "    }",
    "  }",
    "",
    "  return (",
    '    <section className="stack">',
    '      <div className="grid">',
    "        {templates.map((template) => (",
    "          <button",
    '            type="button"',
    "            key={template.id}",
    '            className={selected?.id === template.id ? "wide-card selected" : "wide-card"}',
    "            onClick={() => pick(template)}",
    "          >",
    "            <div",
    "              style={{",
    "                background: template.backgroundColor,",
    "                borderTop: `6px solid ${primaryColor}`,",
    "                fontFamily: headingFont,",
