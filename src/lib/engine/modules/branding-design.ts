// branding-design — in-app branding settings + simple design editing/templates,
// ported from ChurchConnect's real design system:
//   - src/components/ChurchBrandingSettings.tsx: the per-user branding record
//     (church_branding table) with fields church_name, tagline, logo_url,
//     primary_color, secondary_color, accent_color, font_heading, font_body,
//     website_url, phone, address; loaded per user (eq user_id, maybeSingle) and
//     saved as a single-row upsert (insert when none, update when brandingId set);
//     the fixed font option list (Inter, Roboto, Open Sans, Lato, Montserrat,
//     Playfair Display, Merriweather, Georgia, Times New Roman, Arial) and the
//     colors/typography/header live preview.
//   - src/components/InAppDesignEditor.tsx: DESIGN_TEMPLATES (id, name, width,
//     height, category, backgroundColor) — Modern Church Flyer, Instagram Post,
//     Facebook Event Cover, Bulletin Cover — the "choose a template to start
//     designing" pick grid, and saveDesign(), which writes a saved design
//     (canva_design_assets: title, design_type, category, width, height,
//     export_url) scoped to the creator.
// Generalized off the church framing into plain copy so it drops into any app.
// Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_BRANDING); each customer owns their own branding and
// designs (scoped by user.id), admins can see everyone's designs.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

