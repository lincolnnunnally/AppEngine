    "// external provider fetch is dropped — we provision on the app's own DB — but the",
    "// job lifecycle (processing -> completed/failed, request_data, timestamps) and",
    "// the website insert (site_name, site_url, status='active', divi_enabled) are",
    "// ported faithfully.",
    "export async function provisionWebsite(input: ProvisionInput): Promise<ProvisionResult> {",
    '  if (!hasDatabase()) return { ok: false, message: "Connect a database to run provisioning." };',
    '  const domain = (input.domain || "").trim().toLowerCase();',
    '  const siteName = (input.siteName || "").trim();',
    "  if (!domain || !siteName) {",
    '    return { ok: false, message: "A domain and a site name are required to provision." };',
    "  }",
