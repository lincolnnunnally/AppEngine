// business-formation-provisioning — guide a customer from signup into domain,
// email, business formation, and service provisioning. Ported from the real
// "Website-friends" hosting SaaS:
//   - SignupPage.tsx: on signup, create a client profile (full_name, business_name,
//     email, phone, status='active') then a subscription (plan_type='basic',
//     monthly_fee=5.00, status='active', current_period_start/end, next_billing_date
//     one month out). Here the client IS the signed-in user, so we scope the
//     onboarding/client row by users(id) and derive fields from the signup form.
//   - ClientPortal.tsx: the portal that loads a client + subscription + websites +
//     domains + email_accounts + payments + service_requests and shows the
//     provisioning checklist (websites/domains/emails counts, subscription card,
//     recent service requests + payments, per-domain expiry/cost, per-email server
//     settings). Ported the model + the load fan-out + the "What You Get" checklist.
//   - provision-wordpress/index.ts: the provisioning job — record an automation job
//     (job_type='wordpress_setup', status processing→completed/failed, request_data,
//     started_at/completed_at), and on success create a website row (site_name,
//     site_url, status='active', divi_enabled). Generalized off DreamHost/ENOM
//     provider names into a neutral "provisioning" run on the app's own DB.
// Every provider name is dropped from what users see. Runs on the app owner's own
// database with graceful sample-data fallback, feature-flagged (FEATURE_FORMATION);
// customers see their own portal + request services, admins run provisioning +
// review requests.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

