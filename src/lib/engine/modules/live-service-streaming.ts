// live-service-streaming — plan and run a live service/event, then attach a
// stream link + track its status (no media infrastructure required). Ported from
// ChurchConnect's real service + streaming system:
//   - the SERVICE PLAN + run-of-show, ported from WorshipServicePlanner.tsx's
//     ServiceQueue model (service_name, service_date, service_time, items[],
//     status [draft/ready/in_progress/completed], current_item_index,
//     total_duration_seconds) and its QueueItem run-of-show rows (order, title,
//     notes, duration_seconds) — the drag-ordered "service order" list.
//   - the RUN controller, ported from ServiceController.tsx: pick a plan, walk its
//     ordered items (current_item_index), each item carries a type + title +
//     duration + tech notes; the plan moves draft -> in_progress -> completed.
//   - the STREAM session + status, ported verbatim from backend/routes/streaming.py
//     stream_sessions (title, description, status [pre_live -> live -> ended],
//     scheduled_start, actual_start, actual_end, duration_seconds,
//     viewer_count_current/peak, stream_type, chat_enabled, recording_url,
//     started_by/ended_by) plus its real logic: the "already have an active
//     session" guard (start_stream_session), go_live (pre_live -> live, stamp
//     actual_start), stop (compute duration from actual_start, stamp actual_end),
//     and the viewer-count peak bump (update_viewer_count). We drop the RTMP-key /
//     multi-destination broadcast plumbing on purpose — the capability is a stream
//     LINK + status, so a session just carries a stream_url the operator pastes in.
// Generalized off the church framing into plain event copy so it drops into any
// app. Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_STREAMING); customers plan + run their own services and
// go live on their own stream link (rows scoped to user.id), admins see all.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

