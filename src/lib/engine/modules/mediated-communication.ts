    '    [/\\byou always\\b/gi, "it often happens that"],',
    '    [/\\byou never\\b/gi, "it has not yet happened that"],',
    '    [/\\byour fault\\b/gi, "something to resolve together"],',
    '    [/\\bridiculous\\b/gi, "difficult"],',
    '    [/\\bunacceptable\\b/gi, "not workable for me"],',
    '    [/!+/g, "."]',
    "  ];",
    "  let out = message;",
    "  for (const [pattern, value] of replacements) out = out.replace(pattern, value);",
    "  out = out.trim();",
    '  const prefix = "For our child, I want to keep this focused on the schedule. ";',
    "  return out.toLowerCase().startsWith(\"for our child\") ? out : prefix + out;",
