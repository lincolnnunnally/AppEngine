// mediated-communication — AI-assisted neutral, conflict-reducing co-parenting
// communication with exchange notes and conflict tracking. Ported from
// childfirst-solutions' real source:
//   - src/services/message-tools.ts: RewriteRequest { originalMessage },
//     rewriteMessageCalmly() -> { rewritten, original }, and extractActionItems()
//     -> { requests, concerns, summary } (the calm-rewrite + action-extraction
//     tools; the source degrades when the AI provider is not wired, so we read
//     OPENAI_API_KEY and fall back to a deterministic neutral rewrite when unset).
//   - src/services/communication-service.ts: communication events pulled from
//     case_events where event_type='communication', newest-first — kept here as
//     the exchange-note log.
//   - src/components/ExchangeNotesCard.tsx: exchange notes { title, detail }.
//   - src/types/coordination-issue.ts + src/services/coordination-issue-service.ts:
//     the CoordinationIssue model (id, caseId, createdByProfileId, title,
//     description, relatedEventId?, severity[low|moderate|high],
//     status[flagged|review_requested|clarification_requested|resolved|
//     unresolved|escalated], aiSummary?, createdAt), newest-first — the conflict
//     tracker.
//   - src/components/ConflictReductionCard.tsx + CommunicationInsightsCard.tsx +
//     src/app/(app)/ai-insights/page.tsx: the conflict-reduction recommendations
//     and communication insights copy.
// Generalized off the "case/parent_case" framing to a single owner (user.id).
// Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_MEDIATED_COMM); customers use their own tools + notes
// + issues, admins triage every issue.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

