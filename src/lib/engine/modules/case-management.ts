// ---- src/app/api/cases/route.ts (list + create cases) ----------------------

function casesApiFile(): GeneratedModuleFile {
  return file("src/app/api/cases/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessAdmin, canAccessCustomerArea } from "@/lib/auth/roles";',
    'import { listCases, createCase, caseManagementEnabled } from "@/lib/db/case-management";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "export async function GET() {",
    '  if (!caseManagementEnabled()) return NextResponse.json({ ok: false, message: "Case management is turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role)) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  // Admins see every case; customers see only their own.",
    "  const ownerId = canAccessAdmin(user?.role) ? null : (user?.id ? String(user.id) : \"\");",
    "  const cases = await listCases(ownerId);",
    "  return NextResponse.json({ ok: true, cases });",
    "}",
    "",
    "export async function POST(request: Request) {",
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role) || !user?.id) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const caseName = typeof body.caseName === "string" ? body.caseName.trim() : "";',
    "  if (caseName.length < 2) {",
    '    return NextResponse.json({ ok: false, message: "Give the case a name." }, { status: 400 });',
    "  }",
    "  const created = await createCase({",
    "    ownerId: String(user.id),",
    "    caseName,",
    '    childName: typeof body.childName === "string" ? body.childName.trim() : undefined',
    "  });",
    '  if (!created) return NextResponse.json({ ok: false, message: "Connect a database to save cases." }, { status: 503 });',
    "  return NextResponse.json({ ok: true, case: created });",
    "}"
  ]);
}

