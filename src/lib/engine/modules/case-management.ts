// case-management — case timelines, a document center, court-ready summaries, and
// an agreement/attendance tracker, ported from childfirst-solutions' real
// co-parenting workspace:
//   - src/components/CaseTimeline.tsx + src/app/(app)/timeline/page.tsx +
//     src/types/events.ts (TimelineEvent) + src/types/case.ts (CaseEvent.eventType)
//     — the documented-event timeline: title, type (exchange/school/schedule/
//     medical/incident/pickup/communication/expense/request/violation), timestamp,
//     description, scoped to a parent case.
//   - src/components/DocumentCenter.tsx + src/types/events.ts (EventAttachment) —
//     upload + organize parenting plans, schedules, reports, supporting docs by
//     name + type (PDF/Image/...).
//   - src/components/CourtReadySummaryCard.tsx — court-ready summary rows: a
//     category (Parenting Time / Communication / Activities) + a detail line
//     ("92% schedule completion rate"), here derived live from the tracked data.
//   - src/components/AgreementTrackerCard.tsx — agreement/attendance tracker: a
//     topic (Summer Vacation Week, Pickup Adjustment) + a status (Accepted /
//     Pending Approval / Declined).
// The childfirst components were static sample arrays; this port keeps their exact
// model + copy and makes them real — persisted per owner on the app's own DB with
// a graceful sample-data fallback, feature-flagged (FEATURE_CASES). Customers own
// and manage their own cases, events, documents, and agreements; admins can review
// across owners.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

