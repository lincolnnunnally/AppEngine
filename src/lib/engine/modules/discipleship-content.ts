// discipleship-content — reading plans, daily devotionals, daily motivation, and
// kindness challenges, each with per-user progress tracking. Ported from
// ChurchConnect's real backend:
//   - backend/routes/reading_plans.py + reading_plans_data.py — the PLAN_CATALOG
//     (id / name / tagline / description / category / duration_days / pace), the
//     reading_plan_progress model (started_at, current_day, completed_days, streak,
//     longest_streak, paused), start_plan / complete_day / uncomplete_day with the
//     calendar-based streak logic and "advance current_day to smallest incomplete
//     day" rule.
//   - backend/routes/devotionals.py — the daily devotional model (title,
//     opening_prayer, scripture_reference/text, spirit_speaks, reflection_questions,
//     todays_response, closing_prayer, key_theme, date_key) and /devotionals/daily
//     ("today's or most recent") + /devotionals/recent.
//   - backend/routes/challenges.py — the CHALLENGE_LIBRARY (title/description/
//     category/difficulty/points/type), CHALLENGE_CATEGORIES, /complete with the
//     streak-bonus points (min(streak*2, 20)) and the per-user user_streaks model.
//   - backend/routes/discipleship_routes.py — the discipleship "daily motivation"
//     framing (warm, jargon-free growth prompts) surfaced as a daily encouragement.
// Generalized off ChurchConnect's church-only framing into plain, warm copy so it
// drops into any app. Runs on the app owner's own database with graceful sample-data
// fallback, feature-flagged (FEATURE_CONTENT); customers read + track progress,
// admins publish devotionals + challenges.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

