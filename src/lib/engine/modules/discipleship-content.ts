function completeButtonFile(): GeneratedModuleFile {
  return file("src/app/content/challenges/complete-button.tsx", [
    '"use client";',
    "",
    'import { useState } from "react";',
    'import { useRouter } from "next/navigation";',
    "",
    "export function CompleteChallengeButton({ challengeId }: { challengeId: string }) {",
    "  const router = useRouter();",
    '  const [status, setStatus] = useState<"idle" | "saving" | "error">("idle");',
    '  const [message, setMessage] = useState<string>("");',
    "",
    "  async function complete() {",
    '    setStatus("saving");',
    '    setMessage("");',
    "    try {",
    '      const response = await fetch("/api/content/challenges", {',
    '        method: "POST",',
    '        headers: { "Content-Type": "application/json" },',
    "        body: JSON.stringify({ challengeId })",
    "      });",
    "      const data = await response.json();",
    "      if (!response.ok || !data.ok) {",
    '        setStatus("error");',
    '        setMessage(data.message || "Could not complete this challenge.");',
    "        return;",
    "      }",
    "      router.refresh();",
    "    } catch {",
    '      setStatus("error");',
    '      setMessage("Could not reach the server.");',
    "    }",
    "  }",
    "",
    "  return (",
    "    <span>",
    '      <button className="button primary" type="button" onClick={complete} disabled={status === "saving"}>',
    '        {status === "saving" ? "Saving…" : "Mark complete"}',
    "      </button>",
    '      {status === "error" ? <span className="note" role="alert"> {message}</span> : null}',
    "    </span>",
    "  );",
    "}"
  ]);
}

