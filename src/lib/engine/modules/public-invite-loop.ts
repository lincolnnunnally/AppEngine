// public-invite-loop — trusted-peer invitation, ported from Kindred-Connection's
// real "bring-a-friend" system (backend/routers/invites.py + frontend/src/pages/
// Invite.js + PublicInvite.js). We keep its actual model and logic: a per-user
// 8-char invite code (idempotent, unique — get_or_create_invite_code), a public
// /invited-by/{code} landing page keyed by that code, the SEVEN situational
// pre-written message variants (lonely / breakup / tired_of_apps / drifting /
// new_season / spiritually_searching / generic — exact keys, labels, subheads,
// bodies with {first} / {url} tokens), share-tracking by variant_key + method
// (sms | native | copy), joined/onboarded invitee counts, and signup attribution
// (invited_by_user_id). Generalized from "Kindred" to any app: no provider names
// shown to users, runs on the app owner's own DB with graceful fallback, gated by
// FEATURE_INVITES. Members see & send their invites; the public sees the landing
// page; admins read share analytics. Keyed on the signed-in user's email (the
// stable identifier CurrentUser exposes).

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

