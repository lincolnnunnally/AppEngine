    "function parseJson<T>(value: unknown, fallback: T): T {",
    "  if (value == null) return fallback;",
    '  if (typeof value === "object") return value as T;',
    '  if (typeof value === "string") {',
    "    try { return JSON.parse(value) as T; } catch { return fallback; }",
