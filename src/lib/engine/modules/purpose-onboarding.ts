    "  coreValues: string[];",
    "  growthGoals: string[];",
    "  drivers: string;",
    "  reflection: string;",
    "  motivation?: string;",
    "  motivationText?: string;",
    "  lookingFor?: string;",
    "};",
    "",
    "// Ported from Onboarding.js canNext gates — the minimum a real onboarding must",
    "// carry before it means anything.",
    "export function validateOnboarding(input: Partial<OnboardingInput>): string | null {",
    '  if (!input.purpose || input.purpose.trim().length <= 10) return "Say a little more about your purpose.";',
    "  const values = (input.coreValues || []).filter((v) => v && v.trim());",
