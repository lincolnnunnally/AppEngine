    '    reflection: typeof body.reflection === "string" ? body.reflection.trim() : "",',
    '    motivation: typeof body.motivation === "string" ? body.motivation : "",',
    '    motivationText: typeof body.motivationText === "string" ? body.motivationText : "",',
    '    lookingFor: typeof body.lookingFor === "string" ? body.lookingFor : ""',
    "  };",
    "  const problem = validateOnboarding(input);",
    "  if (problem) return NextResponse.json({ ok: false, message: problem }, { status: 400 });",
