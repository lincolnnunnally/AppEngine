// purpose-onboarding — a deep first-run capture of a person's purpose, values,
// growth goals, drivers, reflection and honest motivation, ported from
// Kindred-Connection's real onboarding system (backend/server.py OnboardingIn /
// ProfileUpdate models + POST /onboarding, GET+PATCH /profile/me,
// POST /profile/reanalyze; frontend/src/pages/Onboarding.js multi-step capture +
// VALUE_OPTIONS; Profile.js insights display). We keep its actual data model —
// purpose, core_values, growth_goals[], drivers, reflection, motivation +
// motivation_text, looking_for, and a derived "insights" read (archetype /
// summary / strengths / growth_edges / recommended_path) — and its real logic:
// one profile per user, submitting marks them onboarded, the same
// analyze-purpose pass runs on submit and can be re-run ("reanalyze"). The AI
// soul-read is replaced with a deterministic, provider-free heuristic so the
// module runs on the app owner's own database with graceful sample-data
// fallback, no external keys, and no provider names shown to users.
// Feature-flagged (FEATURE_ONBOARDING); customers complete/read their own
// onboarding, admins can review any profile.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

