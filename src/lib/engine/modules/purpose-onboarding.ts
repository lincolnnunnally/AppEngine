    "  if (!hasDatabase()) return null;",
    "  const insights = deriveInsights(input);",
    "  const completeness = calculateCompleteness(input);",
    "  const values = input.coreValues.filter((v) => v && v.trim());",
    "  const goals = input.growthGoals.filter((g) => g && g.trim());",
    "  const sql = getDatabase();",
    "  const existing = await sql`select id from onboarding_profiles where user_id = ${userId} limit 1`;",
    "  if (existing[0]) {",
    "    await sql`",
    "      update onboarding_profiles set",
    "        purpose = ${input.purpose},",
    "        core_values = ${JSON.stringify(values)},",
    "        growth_goals = ${JSON.stringify(goals)},",
    "        drivers = ${input.drivers},",
    "        reflection = ${input.reflection},",
    '        motivation = ${input.motivation || ""},',
    '        motivation_text = ${input.motivationText || ""},',
    '        looking_for = ${input.lookingFor || ""},',
    "        onboarded = true,",
    "        insights = ${JSON.stringify(insights)},",
    "        completeness = ${completeness},",
    "        updated_at = now()",
