// ai-assist — AI services for content generation and suggestions, ported from
// ChurchConnect's real AI layer (backend/routes/ai_services.py + the shared
// helpers in backend/routes/__init__.py). We carried over the ACTUAL logic:
//   - the owner's-own-key gate: EMERGENT_LLM_KEY / OPENAI_API_KEY read from env,
//     graceful {"error":"AI service not configured"} when unset (never blocks UI).
//   - get_ai_response: call the model with a system prompt + user text, then log
//     usage; get_ai_json_response's resilient JSON extraction (```json fences,
//     bare ```, first-brace-to-last-brace fallback, raw_response last resort).
//   - track_ai_usage: log every call to ai_usage with feature, tokens_estimated,
//     and cost_estimated = round(tokens * 0.00003, 4).
//   - the feature-specific system prompts from ai_chat (scripture_library,
//     devotional, pastoral_care, bible_study, general) — generalized off the
//     church framing into plain content-assistant prompts (drafting, summarizing,
//     brainstorming, rewriting, general) so it drops into any app.
//   - the /api/ai/chat pattern: pick a feature prompt, append optional context,
//     log the request, return the generated text; plus the curate-video style
//     structured JSON suggestion (title/summary/tags) via json mode.
// Runs on the app owner's own database with graceful fallback, feature-flagged
// (FEATURE_AI_ASSIST); customers generate + read their own history, admins see
// the usage/cost ledger. Every table is prefixed ai_assist_ so it can never
// collide with another module's tables.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

