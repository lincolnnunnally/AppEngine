function aiAssistLibFile(): GeneratedModuleFile {
  return file("src/lib/db/ai-assist.ts", [
    'import { getDatabase, hasDatabase } from "@/lib/db/client";',
    "",
    "// A saved AI generation, ported from the ai_chat response shape + ai_usage",
    "// row (feature, tokens_estimated, cost_estimated). Each generation is scoped",
    "// to the owner (ownerId -> users.id) so people only see their own history.",
    "export type AiGeneration = {",
    "  id: string;",
    "  ownerId: string | null;",
    "  feature: string;",
    "  prompt: string;",
    "  context: string;",
    "  output: string;",
    "  tokensEstimated: number;",
    "  costEstimated: number;",
    "  createdAt: string;",
    "};",
    "",
    "// The assist 'features', ported in spirit from ai_chat's feature_additions",
    "// (scripture_library/devotional/pastoral_care/bible_study/general) and",
    "// generalized off the church framing so any app can use them.",
    "export const AI_ASSIST_FEATURES = [",
    '  "draft",',
    '  "summarize",',
    '  "brainstorm",',
    '  "rewrite",',
    '  "suggest",',
    '  "general"',
    "] as const;",
    "",
    "export type AiAssistFeature = (typeof AI_ASSIST_FEATURES)[number];",
    "",
    "// System-prompt additions per feature, ported from ai_chat feature_additions",
    "// and rewritten as a neutral content assistant (no provider name, no domain).",
    "const FEATURE_PROMPTS: Record<string, string> = {",
    '  draft: "You are a helpful writing assistant. Draft clear, well-structured content in the user\'s own voice. Be concrete and useful.",',
    '  summarize: "You are a summarizing assistant. Condense the material into its key points. Be faithful to the source and never invent facts.",',
    '  brainstorm: "You are a brainstorming partner. Offer a varied, practical list of ideas. Favor breadth, then note the two or three strongest.",',
    '  rewrite: "You are an editing assistant. Rewrite the text to be clearer and tighter while preserving the meaning and tone. Return only the rewrite.",',
    '  suggest: "You are a suggestion assistant. Analyze the material and propose a title, a one-to-two sentence summary, and 5-10 relevant tags. Return valid JSON: {\\"title\\": \\"\\", \\"summary\\": \\"\\", \\"tags\\": []}.",',
    '  general: "You are a helpful, honest assistant. Answer plainly and admit when you are unsure."',
    "};",
    "",
    "export function systemPromptFor(feature: string, context?: string): string {",
    "  // Ported from ai_chat: base prompt by feature, then append optional context.",
    "  let prompt = FEATURE_PROMPTS[feature] || FEATURE_PROMPTS.general;",
    '  const ctx = (context || "").trim();',
    "  if (ctx) prompt += `\\n\\nAdditional context: ${ctx}`;",
    "  return prompt;",
    "}",
    "",
    "// The owner's own AI key, ported from routes/__init__.py:",
    "//   EMERGENT_LLM_KEY = os.environ.get('EMERGENT_LLM_KEY') or os.environ.get('OPENAI_API_KEY')",
    "// No provider name is shown to users; the app owner supplies the key.",
    "export function aiKey(): string {",
    '  return (process.env.AI_ASSIST_API_KEY || process.env.OPENAI_API_KEY || "").trim();',
    "}",
    "",
    "export function aiConfigured(): boolean {",
    "  return aiKey().length > 0;",
    "}",
    "",
    "// Cost estimate, ported verbatim from track_ai_usage:",
    "//   cost_estimated = round(tokens_est * 0.00003, 4)",
    "export function estimateCost(tokens: number): number {",
    "  return Math.round(tokens * 0.00003 * 10000) / 10000;",
    "}",
    "",
    "// Resilient JSON extraction, ported from get_ai_json_response: try a ```json",
    "// fence, then a bare ``` fence, then the raw string, then first-brace-to-last;",
    "// fall back to { raw_response } exactly like the source.",
    "export function extractJson(response: string): Record<string, unknown> {",
    "  const candidates: string[] = [];",
    '  if (response.includes("```json")) {',
    '    try { candidates.push(response.split("```json")[1].split("```")[0].trim()); } catch { /* ignore */ }',
    "  }",
    '  if (response.includes("```")) {',
    '    try { candidates.push(response.split("```")[1].split("```")[0].trim()); } catch { /* ignore */ }',
    "  }",
    "  candidates.push(response.trim());",
    '  for (const [open, close] of [["{", "}"], ["[", "]"]] as const) {',
    "    const first = response.indexOf(open);",
    "    const last = response.lastIndexOf(close);",
    "    if (first !== -1 && last > first) candidates.push(response.slice(first, last + 1));",
    "  }",
    "  for (const candidate of candidates) {",
    "    try {",
    "      const parsed = JSON.parse(candidate);",
    '      if (parsed && typeof parsed === "object") return parsed as Record<string, unknown>;',
    "    } catch {",
    "      continue;",
    "    }",
    "  }",
    "  return { raw_response: response };",
    "}",
    "",
    "export type AiCallResult = {",
    "  ok: boolean;",
    "  output: string;",
    "  tokensEstimated: number;",
    "  costEstimated: number;",
    "  error?: string;",
    "};",
    "",
    "// Call the model, ported from get_ai_response: require the owner's key, send the",
    "// system + user message, then estimate tokens (len(user_text)+len(response)) and",
    "// cost. Graceful when the key is unset — returns ok:false, never throws to the UI.",
    "// The provider is called over a plain HTTPS chat-completions endpoint using the",
    "// owner's key; no SDK and no provider name are surfaced to users.",
    "export async function generate(",
    "  systemMessage: string,",
    "  userText: string,",
    '  options: { feature?: string; jsonMode?: boolean; maxTokens?: number } = {}',
    "): Promise<AiCallResult> {",
    "  const key = aiKey();",
    "  if (!key) {",
    "    // Ported from get_ai_response's 'AI service not configured' guard.",
    '    return { ok: false, output: "", tokensEstimated: 0, costEstimated: 0, error: "AI service not configured" };',
    "  }",
    "  const maxTokens = Math.min(4096, Math.max(256, options.maxTokens || 4096));",
    "  try {",
    '    const base = (process.env.AI_ASSIST_BASE_URL || "https://api.openai.com/v1").replace(/\\/$/, "");',
    '    const model = process.env.AI_ASSIST_MODEL || "gpt-4o";',
    "    const body: Record<string, unknown> = {",
    "      model,",
    "      max_tokens: maxTokens,",
    "      messages: [",
    '        { role: "system", content: systemMessage },',
    '        { role: "user", content: userText }',
    "      ]",
    "    };",
    '    if (options.jsonMode) body.response_format = { type: "json_object" };',
    "    const response = await fetch(`${base}/chat/completions`, {",
    '      method: "POST",',
    '      headers: { "Content-Type": "application/json", Authorization: `Bearer ${key}` },',
    "      body: JSON.stringify(body)",
    "    });",
    "    if (!response.ok) {",
    "      const detail = await response.text().catch(() => \"\");",
    '      return { ok: false, output: "", tokensEstimated: 0, costEstimated: 0, error: `AI service error: ${response.status} ${detail.slice(0, 200)}` };',
    "    }",
    "    const data = (await response.json()) as {",
    "      choices?: Array<{ message?: { content?: string } }>;",
    "      usage?: { total_tokens?: number };",
    "    };",
    '    const output = data.choices?.[0]?.message?.content || "";',
    "    // Ported estimate: prefer the provider's token count, else len(input)+len(output).",
    "    const tokens = data.usage?.total_tokens || userText.length + output.length;",
    "    return { ok: true, output, tokensEstimated: tokens, costEstimated: estimateCost(tokens) };",
    "  } catch (error) {",
    "    const message = error instanceof Error ? error.message : String(error);",
    '    return { ok: false, output: "", tokensEstimated: 0, costEstimated: 0, error: `AI service error: ${message}` };',
    "  }",
    "}",
    "",
    "function rowToGeneration(row: Record<string, unknown>): AiGeneration {",
    "  return {",
    "    id: String(row.id),",
    "    ownerId: row.owner_id != null ? String(row.owner_id) : null,",
    '    feature: String(row.feature || "general"),',
    '    prompt: String(row.prompt || ""),',
    '    context: String(row.context || ""),',
    '    output: String(row.output || ""),',
    "    tokensEstimated: Number(row.tokens_estimated || 0),",
    "    costEstimated: Number(row.cost_estimated || 0),",
    '    createdAt: row.created_at ? String(row.created_at) : ""',
    "  };",
    "}",
    "",
    "const fallbackGenerations: AiGeneration[] = [",
    "  {",
    '    id: "sample-1",',
    "    ownerId: null,",
    '    feature: "draft",',
    '    prompt: "Draft a warm welcome message for new members.",',
    '    context: "",',
    '    output: "Welcome! We are so glad you are here. Take your time settling in — reach out any time and someone will be glad to help you find your way.",',
    "    tokensEstimated: 96,",
    "    costEstimated: 0.0029,",
    '    createdAt: "2025-01-01T00:00:00.000Z"',
    "  }",
    "];",
    "",
    "// Log every call, ported from track_ai_usage: this row IS the usage ledger the",
    "// admin cost view reads. On no DB it is a no-op (best-effort), like the source.",
    "export async function logGeneration(input: {",
    "  ownerId?: string | null;",
    "  feature: string;",
    "  prompt: string;",
    "  context?: string;",
    "  output: string;",
    "  tokensEstimated: number;",
    "  costEstimated: number;",
    "}): Promise<AiGeneration | null> {",
    "  if (!hasDatabase()) return null;",
    "  try {",
    "    const sql = getDatabase();",
    "    const rows = await sql`",
    "      insert into ai_assist_generations (",
    "        owner_id, feature, prompt, context, output, tokens_estimated, cost_estimated",
    "      ) values (",
    "        ${input.ownerId || null}, ${input.feature}, ${input.prompt},",
    '        ${input.context || ""}, ${input.output}, ${input.tokensEstimated}, ${input.costEstimated}',
    "      )",
    "      returning id, owner_id, feature, prompt, context, output, tokens_estimated, cost_estimated, created_at`;",
    "    return rows[0] ? rowToGeneration(rows[0]) : null;",
    "  } catch {",
    "    return null;",
    "  }",
    "}",
    "",
    "// A person's own generation history, newest first (scoped to ownerId).",
    "export async function listGenerations(ownerId: string, limit = 50): Promise<AiGeneration[]> {",
    "  const capped = Math.min(200, Math.max(1, limit));",
    "  if (!hasDatabase()) return fallbackGenerations.slice(0, capped);",
    "  try {",
    "    const sql = getDatabase();",
    "    const rows = await sql`",
    "      select id, owner_id, feature, prompt, context, output, tokens_estimated, cost_estimated, created_at",
    "      from ai_assist_generations",
    "      where owner_id = ${ownerId}",
    "      order by created_at desc",
    "      limit ${capped}`;",
    "    return rows.map(rowToGeneration);",
    "  } catch {",
    "    return fallbackGenerations.slice(0, capped);",
    "  }",
    "}",
    "",
    "export async function getGeneration(id: string, ownerId: string): Promise<AiGeneration | null> {",
    "  if (!hasDatabase()) return fallbackGenerations.find((g) => g.id === id) || null;",
    "  try {",
    "    const sql = getDatabase();",
    "    const rows = await sql`",
    "      select id, owner_id, feature, prompt, context, output, tokens_estimated, cost_estimated, created_at",
    "      from ai_assist_generations where id = ${id} and owner_id = ${ownerId} limit 1`;",
    "    return rows[0] ? rowToGeneration(rows[0]) : null;",
    "  } catch {",
    "    return null;",
    "  }",
    "}",
    "",
    "export type AiUsageSummary = {",
    "  totalCalls: number;",
    "  totalTokens: number;",
    "  totalCost: number;",
    "  byFeature: Array<{ feature: string; calls: number; cost: number }>;",
    "};",
    "",
    "// The usage/cost ledger, ported from admin.py get_ai_usage + get_admin_stats:",
    "//   total_ai_calls = count(ai_usage); total_ai_cost = sum(cost_estimated).",
    "export async function usageSummary(): Promise<AiUsageSummary> {",
    "  if (!hasDatabase()) {",
    "    return { totalCalls: 0, totalTokens: 0, totalCost: 0, byFeature: [] };",
    "  }",
    "  try {",
    "    const sql = getDatabase();",
    "    const totals = await sql`",
    "      select count(*)::int as calls,",
    "        coalesce(sum(tokens_estimated), 0)::int as tokens,",
    "        coalesce(sum(cost_estimated), 0)::float as cost",
    "      from ai_assist_generations`;",
    "    const byFeature = await sql`",
    "      select feature, count(*)::int as calls, coalesce(sum(cost_estimated), 0)::float as cost",
    "      from ai_assist_generations",
    "      group by feature",
    "      order by cost desc`;",
    "    const t = totals[0] || {};",
    "    return {",
    "      totalCalls: Number(t.calls || 0),",
    "      totalTokens: Number(t.tokens || 0),",
    "      totalCost: Number(t.cost || 0),",
    "      byFeature: byFeature.map((r: Record<string, unknown>) => ({",
    '        feature: String(r.feature || "general"),',
    "        calls: Number(r.calls || 0),",
    "        cost: Number(r.cost || 0)",
    "      }))",
    "    };",
    "  } catch {",
    "    return { totalCalls: 0, totalTokens: 0, totalCost: 0, byFeature: [] };",
    "  }",
    "}",
    "",
    "export function aiAssistEnabled(): boolean {",
    '  return process.env.FEATURE_AI_ASSIST !== "false";',
    "}"
  ]);
}

// ---- src/app/api/ai-assist/route.ts (generate + list history) ---------------

function aiAssistApiFile(): GeneratedModuleFile {
  return file("src/app/api/ai-assist/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessCustomerArea } from "@/lib/auth/roles";',
    "import {",
    "  aiAssistEnabled,",
    "  aiConfigured,",
    "  systemPromptFor,",
    "  generate,",
    "  logGeneration,",
    "  listGenerations,",
    "  extractJson,",
    "  AI_ASSIST_FEATURES",
    '} from "@/lib/db/ai-assist";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "export async function GET() {",
    '  if (!aiAssistEnabled()) return NextResponse.json({ ok: false, message: "AI assist is turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role) || !user?.id) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const generations = await listGenerations(String(user.id));",
    "  // Report readiness without ever blocking: configured tells the UI if a key is set.",
    "  return NextResponse.json({ ok: true, configured: aiConfigured(), generations });",
    "}",
    "",
    "// Generate content, ported from /api/ai/chat: pick the feature system prompt,",
    "// append optional context, call the model, log usage, and save the result.",
    "export async function POST(request: Request) {",
    '  if (!aiAssistEnabled()) return NextResponse.json({ ok: false, message: "AI assist is turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role) || !user?.id) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const prompt = typeof body.prompt === "string" ? body.prompt.trim() : "";',
    '  const context = typeof body.context === "string" ? body.context.trim() : "";',
    '  const requested = typeof body.feature === "string" ? body.feature : "general";',
    '  const feature = (AI_ASSIST_FEATURES as readonly string[]).includes(requested) ? requested : "general";',
    "  if (prompt.length < 2) {",
    '    return NextResponse.json({ ok: false, message: "Tell the assistant what you need." }, { status: 400 });',
    "  }",
    "  // Graceful degrade, ported from get_ai_response's 'not configured' guard:",
    "  // the UI stays usable, we just say the owner needs to add a key.",
    "  if (!aiConfigured()) {",
    '    return NextResponse.json({ ok: false, configured: false, message: "This app owner has not connected an AI key yet." }, { status: 503 });',
    "  }",
    '  const jsonMode = feature === "suggest";',
    "  const system = systemPromptFor(feature, context);",
    "  const result = await generate(system, prompt, { feature, jsonMode });",
    "  if (!result.ok) {",
    '    return NextResponse.json({ ok: false, message: result.error || "The AI service is unavailable right now." }, { status: 502 });',
    "  }",
    "  // For 'suggest', surface the parsed JSON too (ported from curate-video shape).",
    "  const structured = jsonMode ? extractJson(result.output) : undefined;",
    "  const saved = await logGeneration({",
    "    ownerId: String(user.id),",
    "    feature,",
    "    prompt,",
    "    context,",
    "    output: result.output,",
    "    tokensEstimated: result.tokensEstimated,",
    "    costEstimated: result.costEstimated",
    "  });",
    "  return NextResponse.json({",
    "    ok: true,",
    "    output: result.output,",
    "    structured,",
    "    tokensEstimated: result.tokensEstimated,",
    "    costEstimated: result.costEstimated,",
    "    generation: saved",
    "  });",
    "}"
  ]);
}

