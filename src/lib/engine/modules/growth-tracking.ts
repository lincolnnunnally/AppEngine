// growth-tracking — the per-user progress + streak layer. Track habits, goals,
// reading progress, skills, and milestones over time and keep the momentum with
// calendar-based streaks. This is deliberately the TRACKING layer, NOT a content
// library: it never stores lessons or readings, only a user's own trackables and
// their day-by-day progress.
//
// Ported from ChurchConnect's real progress-tracking code:
//   - backend/routes/reading_plans.py — the per-user progress record
//     (user_id, plan_id, started_at, current_day, completed_days[],
//     last_completed_at, streak, longest_streak, paused), the calendar-based
//     streak math in complete_day (same-day = no change, +1 day = increment,
//     gap > 1 = reset to 1), longest = max(longest, streak), the "advance
//     current_day to the smallest day not yet completed" progression, the
//     uncomplete-day undo, and pause/abandon lifecycle, plus _update_streak
//     (break the streak if the last completion was more than one day ago).
//   - backend/routes/challenges.py — the user_streaks model (current_streak,
//     longest_streak, last_completion_date, total_completions) and the
//     streak_active derivation (last completion >= yesterday), plus the
//     completion-history + by-category rollups and the streak bonus idea.
// Generalized off the church/reading framing into a plain "trackable" model with a
// type (habit / goal / reading / skill / milestone), a target of steps ("days" in
// the source become generic step numbers), and a per-user progress record that
// carries the exact same fields + streak logic. A milestone log (ported from the
// completion history) records each step as it lands.
// Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_TRACKING); customers track their own goals (rows scoped
// to user.id), admins can seed shared trackables and see everyone's activity.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

