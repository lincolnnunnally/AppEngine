// ---- src/app/tracking/[id]/progress-controls.tsx (client: mark steps) -------

function progressControlsFile(): GeneratedModuleFile {
  return file("src/app/tracking/[id]/progress-controls.tsx", [
    '"use client";',
    "",
    'import { useState } from "react";',
    'import { useRouter } from "next/navigation";',
    "",
    "// The step grid + pause/stop controls, ported from reading_plans.py's per-day",
    "// complete/uncomplete taps and the pause/abandon lifecycle. Tapping an incomplete",
    "// step completes it (and bumps the streak); tapping a completed one undoes it.",
    "export function ProgressControls({",
    "  trackableId,",
    "  steps,",
    "  completed,",
    "  nextStep,",
    "  paused",
    "}: {",
    "  trackableId: string;",
    "  steps: number[];",
    "  completed: number[];",
    "  nextStep: number;",
    "  paused: boolean;",
    "}) {",
    "  const router = useRouter();",
    "  const done = new Set(completed);",
    '  const [status, setStatus] = useState<"idle" | "working" | "error">("idle");',
    '  const [message, setMessage] = useState("");',
    "",
    "  async function act(action: string, step?: number) {",
    '    setStatus("working");',
    '    setMessage("");',
    "    try {",
    "      const response = await fetch(`/api/tracking/${trackableId}`, {",
    '        method: "POST",',
    '        headers: { "Content-Type": "application/json" },',
    "        body: JSON.stringify({ action, step })",
    "      });",
    "      const data = await response.json();",
    "      if (!response.ok || !data.ok) {",
    '        setStatus("error");',
    '        setMessage(data.message || "Could not update progress.");',
    "        return;",
    "      }",
    '      setStatus("idle");',
    "      router.refresh();",
    "    } catch {",
    '      setStatus("error");',
    '      setMessage("Could not reach the server.");',
    "    }",
    "  }",
    "",
    "  return (",
    '    <div className="stack">',
    '      <div className="step-grid" role="group" aria-label="Steps">',
    "        {steps.map((step) => {",
    "          const isDone = done.has(step);",
    "          const isNext = step === nextStep && !isDone;",
    "          return (",
    "            <button",
    "              key={step}",
    "              type=\"button\"",
    "              className={isDone ? \"step done\" : isNext ? \"step next\" : \"step\"}",
    '              aria-pressed={isDone}',
    "              aria-label={isDone ? `Step ${step}, done — tap to undo` : `Step ${step} — tap to complete`}",
    '              onClick={() => act(isDone ? "uncomplete" : "complete", step)}',
    '              disabled={status === "working"}',
    "            >",
    "              {step}",
    "            </button>",
    "          );",
    "        })}",
    "      </div>",
    '      <div className="stack">',
    "        <button",
    '          className="button"',
    "          type=\"button\"",
    '          onClick={() => act("pause")}',
    '          disabled={status === "working" || paused}',
    "        >",
    '          {paused ? "Paused" : "Pause"}',
    "        </button>",
    "        <button",
    '          className="button"',
    "          type=\"button\"",
    '          onClick={() => act("abandon")}',
    '          disabled={status === "working"}',
    "        >",
    "          Stop tracking",
    "        </button>",
    "      </div>",
    '      {status === "error" ? <p className="note" role="alert">{message}</p> : null}',
    "    </div>",
    "  );",
    "}"
  ]);
}

