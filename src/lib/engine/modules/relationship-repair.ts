// relationship-repair — guided forgiveness, reflection, letter drafting, two-person
// mediation, and connection reflection, ported from Kindred-Connection's real
// module (backend/routers/forgiveness.py + frontend Forgiveness/ForgivenessJourney/
// Mediation/ConnectionReflection pages). We keep its actual model:
//   * a 5-step SOLO forgiveness path (what → cost → untangle → release → path)
//     with the real step-gating (cost needs what; release needs the untangle
//     letter; path needs release) and the three honest doors (release / boundary
//     / repair);
//   * a send-ready LETTER draft/edit derived from the private release entry;
//   * TWO-PARTY mediation on the same four prompts (what_happened /
//     what_i_want_them_to_know / what_i_want_to_hear / what_i_want_to_own),
//     invite → accept → both write → auto-reveal → optional reflection →
//     each chooses rebuild / boundary / part_peacefully → outcome;
//   * a private CONNECTION REFLECTION (three quiet questions: learned /
//     differently / taught_me).
// Generalized off provider names, runs on the app owner's own database with a
// graceful sample-data fallback, feature-flagged (FEATURE_REPAIR). Signed-in
// customers do their own work; admins have the same write surface. No AI keys are
// required — the "untangle" letter, send-ready draft, and mediation reflection use
// the module's own grounded fallback text so the capability is fully functional
// out of the box.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

