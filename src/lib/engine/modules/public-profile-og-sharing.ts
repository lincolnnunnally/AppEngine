// public-profile-og-sharing — public profile pages + rich share previews,
// ported from Kindred-Connection's real "soul page" system
// (backend/routers/public_profile.py + frontend/src/pages/PublicProfile.js).
// We keep its actual model — a public profile keyed by a lowercased @handle, with
// name, avatar, location, purpose, an archetype + "soul read" summary, a sweet-spot
// statement with talents/passions, and core-values / growth-goals / interests lists —
// and its actual behavior: a JSON profile endpoint (public/profile/{handle}), a
// bot-friendly Open Graph + Twitter-Card share page (og/{handle}) with canonical URL
// and image meta, and a Web-Share/clipboard "Share" action. Generalized from
// "Kindred souls" to any app's public member pages, with no provider names shown to
// users. Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_PUBLIC_PROFILE): customers manage their own handle, admins
// manage anyone's, and the share pages are public so link previews render.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

