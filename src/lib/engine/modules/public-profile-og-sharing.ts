// ---- src/app/api/public-profiles/route.ts (list + upsert) -------------------

function publicProfileApiFile(): GeneratedModuleFile {
  return file("src/app/api/public-profiles/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessAdmin, canAccessCustomerArea } from "@/lib/auth/roles";',
    'import { listPublicProfiles, upsertProfile, publicProfileEnabled } from "@/lib/db/public-profiles";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "function toStringList(value: unknown): string[] {",
    "  if (Array.isArray(value)) return value.map((v) => String(v).trim()).filter(Boolean);",
    '  if (typeof value === "string") return value.split(",").map((v) => v.trim()).filter(Boolean);',
    "  return [];",
    "}",
    "",
    "export async function GET(request: Request) {",
    '  if (!publicProfileEnabled()) return NextResponse.json({ ok: false, message: "Public profiles are turned off for this app." }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!canAccessCustomerArea(user?.role)) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const url = new URL(request.url);",
    '  const profiles = await listPublicProfiles({ q: url.searchParams.get("q") || undefined });',
    "  return NextResponse.json({ ok: true, profiles });",
    "}",
    "",
    "// Save a profile. A signed-in customer may save their OWN profile; admins may save",
    "// on behalf of any user by passing userId.",
    "export async function POST(request: Request) {",
    "  const user = await getCurrentUser();",
    '  if (!user || !canAccessCustomerArea(user.role)) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const isAdmin = canAccessAdmin(user.role);',
    '  const targetUserId = isAdmin && typeof body.userId === "string" && body.userId.trim()',
    "    ? body.userId.trim()",
    "    : user.email;",
    '  const handle = typeof body.handle === "string" ? body.handle : "";',
    '  const name = typeof body.name === "string" ? body.name.trim() : "";',
    '  if (!handle.trim()) return NextResponse.json({ ok: false, message: "Pick a handle for your page." }, { status: 400 });',
    '  if (!name) return NextResponse.json({ ok: false, message: "A display name is required." }, { status: 400 });',
    "  const result = await upsertProfile({",
    "    userId: targetUserId,",
    "    handle,",
    "    name,",
    '    picture: typeof body.picture === "string" ? body.picture : undefined,',
    '    location: typeof body.location === "string" ? body.location : undefined,',
    '    purpose: typeof body.purpose === "string" ? body.purpose : undefined,',
    '    archetype: typeof body.archetype === "string" ? body.archetype : undefined,',
    '    summary: typeof body.summary === "string" ? body.summary : undefined,',
    '    sweetSpot: typeof body.sweetSpot === "string" ? body.sweetSpot : undefined,',
    "    coreValues: toStringList(body.coreValues),",
    "    growthGoals: toStringList(body.growthGoals),",
    "    interests: toStringList(body.interests),",
    "    talents: toStringList(body.talents),",
    "    passions: toStringList(body.passions),",
    '    published: body.published === false ? false : true',
    "  });",
    "  if (!result.ok) {",
    '    if (result.reason === "handle-taken") return NextResponse.json({ ok: false, message: "That handle is already taken." }, { status: 409 });',
    '    if (result.reason === "bad-handle") return NextResponse.json({ ok: false, message: "Use letters, numbers, dots, dashes or underscores." }, { status: 400 });',
    '    if (result.reason === "no-db") return NextResponse.json({ ok: false, message: "Connect a database to save your profile." }, { status: 503 });',
    '    return NextResponse.json({ ok: false, message: "Could not save your profile." }, { status: 500 });',
    "  }",
    "  return NextResponse.json({ ok: true });",
    "}"
  ]);
}

