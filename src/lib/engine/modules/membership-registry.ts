// membership-registry — a free membership program with a verifiable seal,
// factored from the United Under God SEAL built 2026-07-10 on the live
// uug-website repo (src/lib/seal.ts, /join, /members/[slug], /badge/[slug],
// public/seal.js, the /admin membership queue) against the shared LPL
// Supabase. Generalized here to the factory's Next+Neon shape: organizations
// apply through a statement-of-intent join flow, the app owner approves each
// one from a review queue, approved members get a public verification page,
// a directory listing, and an embeddable badge that renders ONLY while
// membership is active and always links back to the member page — the link
// is the verification. The owner is the gate on who displays the seal.
// Membership is free by design; the statement-of-intent copy emitted below is
// a neutral placeholder for the app owner to make their own (doctrine never
// ships from the factory).

import type { AppModule, AppModuleContext, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

// ---- src/lib/db/membership.ts (model, queries, badge, snippets) -------------

function membershipLibFile(ctx: AppModuleContext): GeneratedModuleFile {
  const appName = JSON.stringify(ctx.projectName);
  return file("src/lib/db/membership.ts", [
    'import { getDatabase, hasDatabase } from "@/lib/db/client";',
    "",
    `export const MEMBERSHIP_APP_NAME = ${appName};`,
    "",
    "// What a member organization affirms when it joins. Placeholder wording —",
    "// the app owner should make this statement their own before launch.",
    "export const STATEMENT_OF_INTENT = [",
    `  \`Our organization chooses to stand with the \${MEMBERSHIP_APP_NAME} community.\`,`,
    "  \"We choose to operate openly and treat others the way we want to be treated.\",",
    "  \"We choose to be counted publicly among the organizations that share this intent.\"",
    "];",
    "",
    "export const ORG_TYPES = [\"church\", \"business\", \"charity\", \"association\", \"other\"] as const;",
    "",
    "export const ORG_TYPE_LABELS: Record<string, string> = {",
    "  church: \"Church\",",
    "  business: \"Business\",",
    "  charity: \"Charity\",",
    "  association: \"Association\",",
    "  other: \"Organization\"",
    "};",
    "",
    "export type MemberOrganization = {",
    "  id: string;",
    "  name: string;",
    "  orgType: string;",
    "  website: string;",
    "  contactName: string;",
    "  contactEmail: string;",
    "  message: string;",
    "  status: \"pending\" | \"member\";",
    "  slug: string;",
    "  createdAt: string;",
    "  joinedAt: string | null;",
    "};",
    "",
    "const fallbackMembers: MemberOrganization[] = [",
    "  { id: \"sample-1\", name: \"First Sample Fellowship\", orgType: \"church\", website: \"https://example.org\", contactName: \"Sample Contact\", contactEmail: \"sample@example.org\", message: \"\", status: \"member\", slug: \"first-sample-fellowship\", createdAt: new Date(0).toISOString(), joinedAt: new Date(0).toISOString() }",
    "];",
    "",
    "function rowToOrganization(row: Record<string, unknown>): MemberOrganization {",
    "  return {",
    "    id: String(row.id),",
    "    name: String(row.name || \"\"),",
    "    orgType: String(row.org_type || \"other\"),",
    "    website: String(row.website || \"\"),",
    "    contactName: String(row.contact_name || \"\"),",
    "    contactEmail: String(row.contact_email || \"\"),",
    "    message: String(row.message || \"\"),",
    "    status: row.status === \"member\" ? \"member\" : \"pending\",",
    "    slug: String(row.slug || \"\"),",
    "    createdAt: String(row.created_at || \"\"),",
    "    joinedAt: row.joined_at ? String(row.joined_at) : null",
    "  };",
    "}",
    "",
    "export function membershipEnabled(): boolean {",
    "  return process.env.FEATURE_MEMBERSHIP !== \"false\";",
    "}",
    "",
    "export function memberSince(joinedAt: string | null): string {",
    "  if (!joinedAt) return \"\";",
    "  return new Date(joinedAt).toLocaleDateString(\"en-US\", { month: \"long\", year: \"numeric\" });",
    "}",
    "",
    "function slugify(name: string): string {",
    "  const base = name.toLowerCase().replace(/[^a-z0-9]+/g, \"-\").replace(/(^-+|-+$)/g, \"\").slice(0, 80);",
    "  return base || \"organization\";",
    "}",
    "",
    "// Public read: approved members only, newest-joined last (founding first).",
    "export async function listMembers(): Promise<MemberOrganization[]> {",
    "  if (!hasDatabase()) return fallbackMembers;",
    "  try {",
    "    const sql = getDatabase();",
    "    const rows = await sql`select id, name, org_type, website, slug, status, created_at, joined_at, '' as contact_name, '' as contact_email, '' as message from membership_registry_organizations where status = 'member' order by joined_at asc, name asc limit 1000`;",
    "    return rows.map(rowToOrganization);",
    "  } catch {",
    "    return [];",
    "  }",
    "}",
    "",
    "// Public read of one member by slug — contact details deliberately withheld.",
    "export async function getMemberBySlug(slug: string): Promise<MemberOrganization | null> {",
    "  if (!hasDatabase()) return fallbackMembers.find((member) => member.slug === slug) || null;",
    "  try {",
    "    const sql = getDatabase();",
    "    const rows = await sql`select id, name, org_type, website, slug, status, created_at, joined_at, '' as contact_name, '' as contact_email, '' as message from membership_registry_organizations where slug = ${slug} and status = 'member' limit 1`;",
    "    return rows[0] ? rowToOrganization(rows[0]) : null;",
    "  } catch {",
    "    return null;",
    "  }",
    "}",
    "",
    "// Owner read: everything, applications first.",
    "export async function listAllOrganizations(): Promise<MemberOrganization[]> {",
    "  if (!hasDatabase()) return fallbackMembers;",
    "  try {",
    "    const sql = getDatabase();",
    "    const rows = await sql`select * from membership_registry_organizations order by (status = 'pending') desc, created_at desc limit 2000`;",
    "    return rows.map(rowToOrganization);",
    "  } catch {",
    "    return [];",
    "  }",
    "}",
    "",
    "export type MembershipApplication = {",
    "  name: string; orgType: string; website?: string;",
    "  contactName: string; contactEmail: string; message?: string;",
    "};",
    "",
    "// Applications always arrive pending — approval is the owner's alone. The",
    "// slug is minted here with a dedupe loop so every member page URL is stable",
    "// and collision-free.",
    "export async function applyForMembership(input: MembershipApplication): Promise<boolean> {",
    "  if (!hasDatabase()) return false;",
    "  const sql = getDatabase();",
    "  const base = slugify(input.name);",
    "  let slug = base;",
    "  for (let n = 2; n < 200; n += 1) {",
    "    const existing = await sql`select id from membership_registry_organizations where slug = ${slug} limit 1`;",
    "    if (!existing[0]) break;",
    "    slug = `${base}-${n}`;",
    "  }",
    "  const orgType = (ORG_TYPES as readonly string[]).includes(input.orgType) ? input.orgType : \"other\";",
    "  await sql`insert into membership_registry_organizations (name, org_type, website, contact_name, contact_email, message, status, slug) values (${input.name}, ${orgType}, ${input.website || \"\"}, ${input.contactName}, ${input.contactEmail}, ${input.message || \"\"}, 'pending', ${slug})`;",
    "  return true;",
    "}",
    "",
    "// Approve (member page + badge go live) or revoke (both die immediately).",
    "export async function setMembershipStatus(id: string, status: \"pending\" | \"member\"): Promise<boolean> {",
    "  if (!hasDatabase()) return false;",
    "  const sql = getDatabase();",
    "  if (status === \"member\") {",
    "    await sql`update membership_registry_organizations set status = 'member', joined_at = now() where id = ${id}`;",
    "  } else {",
    "    await sql`update membership_registry_organizations set status = 'pending', joined_at = null where id = ${id}`;",
    "  }",
    "  return true;",
    "}",
    "",
    "export async function removeOrganization(id: string): Promise<boolean> {",
    "  if (!hasDatabase()) return false;",
    "  const sql = getDatabase();",
    "  await sql`delete from membership_registry_organizations where id = ${id}`;",
    "  return true;",
    "}",
    "",
    "// Origin for embed snippets; empty string falls back to relative URLs.",
    "export function siteOrigin(): string {",
    "  return (process.env.APP_URL || process.env.AUTH_URL || \"\").replace(/\\/$/, \"\");",
    "}",
    "",
    "// The badge itself — self-contained colors so it reads on any host site.",
    "// Served only for active members; the link back to the member page is the",
    "// verification.",
    "export function badgeSvg(): string {",
    "  const ring = MEMBERSHIP_APP_NAME.toUpperCase().slice(0, 26);",
    "  return `<svg xmlns=\"http://www.w3.org/2000/svg\" viewBox=\"0 0 200 200\" role=\"img\" aria-label=\"Verified member\">",
    "  <title>Verified member of ${ring}</title>",
    "  <circle cx=\"100\" cy=\"100\" r=\"100\" fill=\"#101a33\"/>",
    "  <circle cx=\"100\" cy=\"100\" r=\"96\" fill=\"none\" stroke=\"#faf9f6\" stroke-width=\"2.5\"/>",
    "  <circle cx=\"100\" cy=\"100\" r=\"66\" fill=\"none\" stroke=\"#faf9f6\" stroke-width=\"1\"/>",
    "  <defs>",
    "    <path id=\"badge-arc-top\" d=\"M 18 100 A 82 82 0 0 1 182 100\" fill=\"none\"/>",
    "    <path id=\"badge-arc-bottom\" d=\"M 24 100 A 76 76 0 0 0 176 100\" fill=\"none\"/>",
    "  </defs>",
    "  <text font-size=\"12\" font-weight=\"700\" letter-spacing=\"2\" fill=\"#faf9f6\" font-family=\"system-ui, sans-serif\">",
    "    <textPath href=\"#badge-arc-top\" startOffset=\"50%\" text-anchor=\"middle\">${ring}</textPath>",
    "  </text>",
    "  <text font-size=\"10.5\" font-weight=\"600\" letter-spacing=\"2.2\" fill=\"#faf9f6\" font-family=\"system-ui, sans-serif\">",
    "    <textPath href=\"#badge-arc-bottom\" startOffset=\"50%\" text-anchor=\"middle\">VERIFIED MEMBER</textPath>",
    "  </text>",
    "  <g stroke=\"#faf9f6\" stroke-width=\"6\" fill=\"none\">",
    "    <circle cx=\"89\" cy=\"112\" r=\"23\"/>",
    "    <circle cx=\"111\" cy=\"112\" r=\"23\"/>",
    "    <path d=\"M100 62v25M88.5 74h23\" stroke-linecap=\"round\"/>",
    "  </g>",
    "</svg>`;",
    "}",
    "",
    "export function htmlSnippet(slug: string): string {",
    "  const origin = siteOrigin();",
    "  return `<a href=\"${origin}/members/${slug}\" target=\"_blank\" rel=\"noopener\">\\n  <img src=\"${origin}/badge/${slug}\" alt=\"Verified member — click to verify\" width=\"140\" height=\"140\" style=\"display:block\" />\\n</a>`;",
    "}"
  ]);
}

// ---- src/app/api/membership/route.ts (public apply + owner list) ------------

function membershipApiFile(): GeneratedModuleFile {
  return file("src/app/api/membership/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessAdmin } from "@/lib/auth/roles";',
    'import { applyForMembership, listAllOrganizations, membershipEnabled } from "@/lib/db/membership";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "export async function GET() {",
    '  if (!membershipEnabled()) return NextResponse.json({ ok: false }, { status: 404 });',
    "  const user = await getCurrentUser();",
    '  if (!user || !canAccessAdmin(user.role)) return NextResponse.json({ ok: false, message: "Admins only." }, { status: 403 });',
    "  const organizations = await listAllOrganizations();",
    "  return NextResponse.json({ ok: true, organizations });",
    "}",
    "",
    "export async function POST(request: Request) {",
    '  if (!membershipEnabled()) return NextResponse.json({ ok: false, message: "Membership is turned off for this app." }, { status: 404 });',
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    "  // Honeypot: bots that fill the hidden field get a quiet yes and no row.",
    '  if (typeof body.hp === "string" && body.hp.trim().length > 0) return NextResponse.json({ ok: true });',
    '  const name = typeof body.name === "string" ? body.name.trim().slice(0, 300) : "";',
    '  const contactName = typeof body.contactName === "string" ? body.contactName.trim().slice(0, 200) : "";',
    '  const contactEmail = typeof body.contactEmail === "string" ? body.contactEmail.trim().slice(0, 320) : "";',
    "  if (name.length < 2) {",
    '    return NextResponse.json({ ok: false, message: "Please add your organization\'s name." }, { status: 400 });',
    "  }",
    '  if (!contactName || !contactEmail.includes("@")) {',
    '    return NextResponse.json({ ok: false, message: "Please add a contact name and a real email." }, { status: 400 });',
    "  }",
    "  const created = await applyForMembership({",
    "    name,",
    "    contactName,",
    "    contactEmail,",
    '    orgType: typeof body.orgType === "string" ? body.orgType : "other",',
    '    website: typeof body.website === "string" ? body.website.trim().slice(0, 500) : undefined,',
    '    message: typeof body.message === "string" ? body.message.trim().slice(0, 4000) : undefined',
    "  });",
    '  if (!created) return NextResponse.json({ ok: false, message: "Connect a database to accept applications." }, { status: 503 });',
    "  return NextResponse.json({ ok: true });",
    "}"
  ]);
}

