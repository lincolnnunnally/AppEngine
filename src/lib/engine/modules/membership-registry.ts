// ---- src/app/membership-queue/actions.tsx (client approve/revoke/remove) ------

function queueActionsFile(): GeneratedModuleFile {
  return file("src/app/membership-queue/actions.tsx", [
    '"use client";',
    "",
    'import { useRouter } from "next/navigation";',
    'import { useState } from "react";',
    "",
    "export function MembershipQueueActions({ id, status }: { id: string; status: \"pending\" | \"member\" }) {",
    "  const router = useRouter();",
    "  const [working, setWorking] = useState(false);",
    "",
    '  async function update(next: "member" | "pending" | "remove") {',
    "    setWorking(true);",
    '    if (next === "remove") {',
    '      await fetch(`/api/membership/${id}`, { method: "DELETE" }).catch(() => null);',
    "    } else {",
    '      await fetch(`/api/membership/${id}`, {',
    '        method: "PATCH",',
    '        headers: { "Content-Type": "application/json" },',
    "        body: JSON.stringify({ status: next })",
    "      }).catch(() => null);",
    "    }",
    "    setWorking(false);",
    "    router.refresh();",
    "  }",
    "",
    "  return (",
    '    <p className="stack">',
    '      {status === "pending" ? (',
    '        <button className="button primary" type="button" disabled={working} onClick={() => void update("member")}>Welcome them in</button>',
    "      ) : (",
    '        <button className="button" type="button" disabled={working} onClick={() => void update("pending")}>Revoke the badge</button>',
    "      )}",
    '      <button className="button" type="button" disabled={working} onClick={() => void update("remove")}>Remove</button>',
    "    </p>",
    "  );",
    "}"
  ]);
}

