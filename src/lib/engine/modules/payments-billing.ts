// ---- src/lib/giving-checkout.ts (Stripe Checkout for gifts) -------------------

function givingCheckoutLibFile(): GeneratedModuleFile {
  return file("src/lib/giving-checkout.ts", [
    "// Giving checkout on the app owner's own Stripe account, ported from",
    "// ChurchConnect stripe_payments.py create_checkout_session (one-time gifts are",
    '// a mode "payment" session built from name + amount). Monthly gifts use',
    "// Checkout's subscription mode with recurring price_data — the generated-app",
    "// equivalent of stripe_billing.py charge-subscription, since these apps keep no",
    "// card vault. Unlike the foundation product checkout, the session id is",
    "// returned so the webhook can settle each gift precisely (ChurchConnect tracks",
    "// payments by intent id the same way). Configuration is shared with the",
    "// foundation payments module (STRIPE_SECRET_KEY).",
    'import { stripeConfigured } from "@/lib/payments";',
    "",
    "export type GivingCheckoutInput = {",
    "  amountCents: number;",
    "  fundName: string;",
    "  monthly: boolean;",
    "  successUrl: string;",
    "  cancelUrl: string;",
    "  donorEmail?: string;",
    "};",
    "",
    "export type GivingCheckoutResult = { ok: boolean; url?: string; sessionId?: string; reason?: string };",
    "",
    "export async function createGivingCheckout(input: GivingCheckoutInput): Promise<GivingCheckoutResult> {",
    "  if (!stripeConfigured()) {",
    '    return { ok: false, reason: "Payments are not connected yet (set STRIPE_SECRET_KEY)." };',
    "  }",
    "  const body = new URLSearchParams();",
    '  body.set("mode", input.monthly ? "subscription" : "payment");',
    '  body.set("success_url", input.successUrl);',
    '  body.set("cancel_url", input.cancelUrl);',
    '  body.set("line_items[0][quantity]", "1");',
    '  body.set("line_items[0][price_data][currency]", "usd");',
    '  body.set("line_items[0][price_data][unit_amount]", String(Math.max(1, Math.round(input.amountCents))));',
    '  body.set("line_items[0][price_data][product_data][name]", (input.monthly ? "Monthly gift — " : "Gift — ") + (input.fundName || "General Fund"));',
    '  if (input.monthly) body.set("line_items[0][price_data][recurring][interval]", "month");',
    '  if (input.donorEmail) body.set("customer_email", input.donorEmail);',
    "  try {",
    '    const response = await fetch("https://api.stripe.com/v1/checkout/sessions", {',
    '      method: "POST",',
    "      headers: {",
    '        authorization: "Bearer " + process.env.STRIPE_SECRET_KEY,',
    '        "content-type": "application/x-www-form-urlencoded"',
    "      },",
    "      body: body.toString()",
    "    });",
    "    const data = (await response.json().catch(() => ({}))) as { id?: string; url?: string; error?: { message?: string } };",
    "    if (!response.ok || !data.url) {",
    '      return { ok: false, reason: (data.error && data.error.message) || ("The payment provider returned " + response.status) };',
    "    }",
    '    return { ok: true, url: data.url, sessionId: data.id || "" };',
    "  } catch {",
    '    return { ok: false, reason: "The payment provider was unreachable." };',
    "  }",
    "}"
  ]);
}

