// payments-billing — online giving, donations, and recurring gifts, ported from
// ChurchConnect's real giving system (src/components/OnlineGiving.tsx give/history
// views, backend/routes/stripe_payments.py checkout-session + webhook status
// tracking, backend/routes/universal_giving.py funds/recurring/yearly stats, and
// backend/routes/stripe_billing.py's recurring charge — adapted to Stripe
// Checkout's subscription mode because generated apps keep no card vault).
//
// Scope is deliberately what the foundation payments module LACKS: foundation
// covers product checkout (src/lib/payments.ts, /api/billing/*, `payments` table);
// this module adds designated-fund giving, donor gift history, monthly recurring
// gifts, and annual giving statements — its own /giving pages, /api/giving routes,
// and payments_billing_* tables, keyed to the same owner Stripe credentials.
// Feature-flagged (FEATURE_GIVING), donors give, admins see fund totals.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

