// supplier-order-automation — forecast supply needs, compare vendor pricing,
// automate the purchase/order flow, and reconcile fulfillment. Ported from
// TonerTrackerPro's real supply-chain code:
//   - server/supplier-service.ts (selectSupplierForOrder / findDefaultSupplier /
//     canSupplierFulfillOrder): pick the vendor for an item — honor a preferred
//     supplier when set, else fall back to the lowest-priced ACTIVE supplier whose
//     catalog covers the item and who can fulfill; the "saves $X vs retail" reason
//     string is ported verbatim from findDefaultSupplier.
//   - server/services/orderAutomation.ts (createOrder / automation modes): the
//     manual | auto | conditional automation mode, the auto-approve rule
//     (auto/conditional auto-approve; manual stays pending), the conditional
//     "toner already on hand -> skip" branch, and the pricing-error -> force
//     pending-review branch, all ported from OrderAutomationService.createOrder.
//   - server/order-fulfillment-service.ts (processApprovedOrder / handleApiOrder /
//     handleManualOrder / createTonerOrder): once an order is approved, route it by
//     supplier capability (api+supportsAutomaticOrdering -> auto task; portal/email
//     -> manual task with a ported email/portal instruction body), stamp the order
//     'ordered', and create the fulfillment task; reconcile = mark received.
//   - attached_assets .../AutoOrderSettings.tsx: the owner settings — enable
//     auto-order, order threshold (days remaining), max auto-order amount, require
//     approval + approval threshold — ported as the org automation config surface.
//   - attached_assets .../SupplierIntegration.tsx: the supplier record (name,
//     contactName, email, phone, priority [1=highest], enabled, notes) and the
//     priority-ordered, email-based supplier list, ported as the supplier registry.
// Generalized off the printer/toner framing into plain "supply item" language so it
// drops into any app. Runs on the app owner's own database with graceful sample-data
// fallback, feature-flagged (FEATURE_SUPPLIER); customers view forecasts + queue +
// suppliers, admins add suppliers, run the forecast, and approve/place/reconcile.

