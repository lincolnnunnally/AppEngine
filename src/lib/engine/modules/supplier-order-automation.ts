    '    return { order: null, action: "failed", reason: "Connect a database to place orders." };',
    "  }",
    "  try {",
    "    const sql = getDatabase();",
    "    const rows = await sql`",
    "      insert into supplier_order_automation_orders (",
    "        owner_id, supplier_id, supplier_name, item_name, quantity, unit_price,",
    "        total_amount, status, trigger_reason, approved_by_user_id, approved_at",
    "      ) values (",
    "        ${ownerId}, ${chosen.supplierId}, ${chosen.supplierName}, ${request.itemName},",
    "        ${quantity}, ${unitPrice}, ${totalAmount},",
    '        ${autoApprove ? "approved" : "pending"}, ${triggerReason},',
    "        null, ${autoApprove ? new Date().toISOString() : null}",
    "      )",
    "      returning id, owner_id, supplier_id, supplier_name, item_name, quantity,",
    "        unit_price, total_amount, status, trigger_reason, fulfillment_method,",
    "        fulfillment_note, approved_by_user_id, approved_at, ordered_at,",
    "        received_at, created_at`;",
    "    const order = rows[0] ? rowToOrder(rows[0]) : null;",
    "    return {",
    "      order,",
    '      action: autoApprove ? "created_approved" : "created_pending",',
    "      reason",
    "    };",
    "  } catch {",
    '    return { order: null, action: "failed", reason: "Could not save the order." };',
    "  }",
    "}",
    "",
    "// Run the forecast: for each need at/below the threshold, create an order per the",
    "// automation config. Ported from OrderAutomationService.createOrders batching +",
    "// AutoOrderSettings threshold gating.",
    "export async function runForecast(ownerId: string, forecasts: SupplyForecast[]): Promise<CreateOrderResult[]> {",
    "  const config = await getAutomationConfig(ownerId);",
    "  const results: CreateOrderResult[] = [];",
    "  for (const f of forecasts) {",
    "    if (f.daysRemaining > config.orderThresholdDays) {",
    '      results.push({ order: null, action: "skipped", reason: `${f.itemName}: ${f.daysRemaining} days left is above the ${config.orderThresholdDays}-day threshold` });',
    "      continue;",
    "    }",
    "    const urgency = forecastUrgency(f.daysRemaining, config.orderThresholdDays);",
    "    const result = await createOrder(ownerId, {",
    "      itemName: f.itemName,",
    "      quantity: Math.max(1, f.suggestedQuantity || 1),",
    "      daysRemaining: f.daysRemaining,",
    "      triggerReason: `Forecast: ${f.daysRemaining} days remaining (${urgency})`",
    "    });",
    "    results.push(result);",
    "  }",
    "  return results;",
    "}",
    "",
    "// Approve a pending order, ported from the createOrder approvedByUserId semantics",
    "// (a real userId here = manual approval, vs null = auto).",
    "export async function approveOrder(id: string, ownerId: string, approverUserId: string): Promise<boolean> {",
    "  if (!hasDatabase()) return false;",
    "  try {",
    "    const sql = getDatabase();",
    "    await sql`",
    "      update supplier_order_automation_orders",
    "      set status = 'approved', approved_by_user_id = ${approverUserId}, approved_at = now()",
    "      where id = ${id} and owner_id = ${ownerId} and status = 'pending'`;",
    "    return true;",
    "  } catch {",
    "    return false;",
    "  }",
    "}",
    "",
    "// Fulfill an approved order, ported from order-fulfillment-service.ts",
    "// processApprovedOrder: route by supplier capability — api + supportsAutomaticOrdering",
    "// -> automatic; portal/email -> a manual instruction note (ported from",
    "// handleManualOrder's portal/email task bodies) — then stamp the order 'ordered'.",
    "export async function fulfillOrder(id: string, ownerId: string): Promise<{ ok: boolean; message: string }> {",
    '  if (!hasDatabase()) return { ok: false, message: "Connect a database to fulfill orders." };',
    "  const order = await getOrder(id, ownerId);",
    '  if (!order) return { ok: false, message: "Order not found." };',
    '  if (order.status !== "approved") return { ok: false, message: "Only approved orders can be fulfilled." };',
    "  const supplier = order.supplierId ? await getSupplier(order.supplierId, ownerId) : null;",
    '  if (!supplier) return { ok: false, message: "Supplier for this order is unavailable." };',
    "",
    '  let method: SupplyOrder["fulfillmentMethod"] = supplier.orderMethod;',
    '  let note = "";',
    '  if (supplier.orderMethod === "api" && supplier.supportsAutomaticOrdering) {',
    "    // handleApiOrder: automatic placement.",
    '    method = "api";',
    "    note = `Automatically placed order via API for ${order.quantity}x ${order.itemName}.`;",
    '  } else if (supplier.orderMethod === "portal") {',
    "    // handleManualOrder portal branch (ported task body).",
    '    method = "portal";',
    "    note = `Place order via supplier portal at ${supplier.portalUrl || supplier.name}: ${order.quantity}x ${order.itemName}, est. $${order.totalAmount.toFixed(2)}.`;",
    "  } else {",
    "    // handleManualOrder email branch (ported task body).",
    '    method = "email";',
    "    note = `Send order email to ${supplier.email}: ${order.quantity}x ${order.itemName}, est. $${order.totalAmount.toFixed(2)}. Please confirm and provide tracking.`;",
    "  }",
    "  try {",
    "    const sql = getDatabase();",
    "    await sql`",
    "      update supplier_order_automation_orders",
    "      set status = 'ordered', ordered_at = now(),",
    "        fulfillment_method = ${method}, fulfillment_note = ${note}",
    "      where id = ${id} and owner_id = ${ownerId} and status = 'approved'`;",
    "    return { ok: true, message: note };",
    "  } catch {",
    '    return { ok: false, message: "Could not fulfill the order." };',
    "  }",
    "}",
    "",
    "// Reconcile fulfillment: mark an ordered item received. Ported from the",
    "// order status flow's terminal transition (ordered -> received).",
    "export async function reconcileOrder(id: string, ownerId: string): Promise<boolean> {",
    "  if (!hasDatabase()) return false;",
    "  try {",
    "    const sql = getDatabase();",
    "    await sql`",
    "      update supplier_order_automation_orders",
    "      set status = 'received', received_at = now()",
    "      where id = ${id} and owner_id = ${ownerId} and status = 'ordered'`;",
    "    return true;",
    "  } catch {",
    "    return false;",
    "  }",
    "}",
