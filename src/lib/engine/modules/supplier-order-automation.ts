    '  action: "created_pending" | "created_approved" | "skipped" | "failed";',
    "  reason: string;",
    "};",
    "",
    "// createOrder, ported from OrderAutomationService.createOrder: pick a supplier",
    "// (lowest price), price the order, apply automation mode (manual -> pending;",
    "// auto/conditional -> approved), and force pending-review when the total exceeds",
    "// the owner's max/approval thresholds (ported from the pricing-error + max-amount",
    "// approval logic in AutoOrderSettings + createOrder).",
    "export async function createOrder(ownerId: string, request: CreateOrderRequest): Promise<CreateOrderResult> {",
    "  const config = await getAutomationConfig(ownerId);",
    "  if (!config.autoOrderEnabled && !request.supplierId) {",
    '    return { order: null, action: "skipped", reason: "Automatic ordering is turned off." };',
    "  }",
    "  const suppliers = await listSuppliers(ownerId);",
    "  const chosen = request.supplierId",
    "    ? compareSuppliers(suppliers).find((c) => c.supplierId === request.supplierId) || null",
    "    : selectSupplier(suppliers);",
    "  if (!chosen) {",
    '    return { order: null, action: "failed", reason: "No enabled supplier available for this item." };',
    "  }",
    "  const quantity = Math.max(1, Math.floor(request.quantity || 1));",
    "  const unitPrice = chosen.unitPrice;",
    "  const totalAmount = Number((unitPrice * quantity).toFixed(2));",
    "",
    "  // Auto-approve rule ported from shouldAutoApprove; approval-threshold + max-order",
    "  // rule ported from AutoOrderSettings.requireApproval/approvalThreshold/maxOrderAmount.",
    "  let autoApprove = shouldAutoApprove(config.mode);",
    "  let reason = autoApprove ? \"Order auto-approved and ready to place\" : \"Order created and awaiting approval\";",
    "  if (autoApprove && config.requireApproval && totalAmount > config.approvalThreshold) {",
    "    autoApprove = false;",
    "    reason = `Order created but needs approval: $${totalAmount.toFixed(2)} exceeds the $${config.approvalThreshold.toFixed(2)} approval threshold`;",
    "  }",
    "  if (autoApprove && totalAmount > config.maxOrderAmount) {",
    "    autoApprove = false;",
    "    reason = `Order created but needs approval: $${totalAmount.toFixed(2)} exceeds the $${config.maxOrderAmount.toFixed(2)} max auto-order amount`;",
    "  }",
    "",
    '  const triggerReason = request.triggerReason || (request.daysRemaining != null ? `Forecast: ${request.daysRemaining} days remaining` : "Manual order");',
