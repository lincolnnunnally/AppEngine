// location-proximity — the shared "what's near me" layer, wired from the code that
// ALREADY EXISTS in this repo: src/lib/geo/location-proximity.ts (the TypeScript
// client for the geo RPCs) and db/location-proximity-schema.sql (geo_places on the
// shared LPL Supabase, PostGIS, SECURITY DEFINER RPCs). See
// source-of-truth/location-proximity-module.md.
//
// This is NOT a per-app Neon table — it is a shared, privacy-hardened SERVICE:
//   * searches run against pre-fuzzed points (write-time deterministic fuzzing);
//   * person-to-person distance is only ever a band, never a number;
//   * the RPCs never return coordinates and are service-role-only, so every geo
//     call happens on the server, never in the browser.
// So this module emits the geo client (ported verbatim from the existing file),
// a thin app helper, and a server-guarded "near me" surface. It owns no tables;
// it activates when the app is given the geo service creds (GEO_SUPABASE_URL +
// GEO_SERVICE_KEY) and degrades gracefully — with no leak — when they are unset.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

// ---- src/lib/geo/location-client.ts (ported verbatim from the repo's client) --

function geoClientFile(): GeneratedModuleFile {
  return file("src/lib/geo/location-client.ts", [
    "// TypeScript client for the shared geo layer, ported from AppEngine's own",
    "// src/lib/geo/location-proximity.ts. Calls the module's SECURITY DEFINER RPCs",
    "// over PostgREST with plain fetch (no SDK). BACKEND-ONLY: the service-role key",
    "// must never reach a browser bundle. The RPCs never return coordinates, so this",
    "// client cannot retrieve them; person-to-person distance is only ever a band.",
    "",
    'export type GeoVisibility = "exact" | "neighborhood" | "city" | "hidden";',
    "",
    "export type GeoSubjectRef = { appSlug: string; subjectType: string; subjectId: string };",
    "",
    "export type GeoUpsertInput = GeoSubjectRef & {",
    "  lat?: number; lng?: number; addressLine?: string; city?: string; region?: string;",
    "  postalCode?: string; country?: string; visibility?: GeoVisibility;",
    '  source?: "nominatim" | "device" | "manual" | "zip_centroid";',
    "};",
    "",
    "export type GeoSearchHit = {",
    "  subject_type: string; subject_id: string;",
    "  distance_m: number | null; distance_band: string | null; city_label: string | null;",
    "};",
    "",
    "// Band thresholds (meters) — MUST match geo_distance_band() in the SQL (5/10/25/50 mi).",
    "export const GEO_BAND_THRESHOLDS_M = [",
    '  { maxM: 8047, band: "< 5 mi" },',
    '  { maxM: 16093, band: "5-10 mi" },',
    '  { maxM: 40234, band: "10-25 mi" },',
    '  { maxM: 80467, band: "25-50 mi" }',
    "] as const;",
    'export const GEO_BAND_BEYOND = "50+ mi";',
    "",
    "export function bandForMeters(meters: number | null | undefined): string | null {",
    "  if (meters === null || meters === undefined || Number.isNaN(meters)) return null;",
    "  for (const { maxM, band } of GEO_BAND_THRESHOLDS_M) if (meters < maxM) return band;",
    "  return GEO_BAND_BEYOND;",
    "}",
    "export function milesToMeters(miles: number): number { return miles * 1609.344; }",
    "",
    "export type GeoClientOptions = { supabaseUrl: string; apiKey: string; fetchImpl?: typeof fetch };",
    "",
    "async function rpc<T>(opts: GeoClientOptions, fn: string, args: Record<string, unknown>): Promise<T> {",
    "  const doFetch = opts.fetchImpl ?? fetch;",
    "  const response = await doFetch(`${opts.supabaseUrl.replace(/\\/$/, \"\")}/rest/v1/rpc/${fn}`, {",
    '    method: "POST",',
    '    headers: { "content-type": "application/json", apikey: opts.apiKey, authorization: `Bearer ${opts.apiKey}` },',
    "    body: JSON.stringify(args)",
    "  });",
    "  if (!response.ok) {",
    '    const detail = await response.text().catch(() => "");',
    "    throw new Error(`geo rpc ${fn} failed: ${response.status} ${detail.slice(0, 300)}`);",
    "  }",
    "  return (await response.json()) as T;",
    "}",
    "",
    "export function createGeoClient(opts: GeoClientOptions) {",
    "  return {",
    "    async upsertPlace(input: GeoUpsertInput): Promise<string> {",
    '      return rpc<string>(opts, "geo_upsert_place", {',
    "        p_app_slug: input.appSlug, p_subject_type: input.subjectType, p_subject_id: input.subjectId,",
    "        p_lat: input.lat ?? null, p_lng: input.lng ?? null, p_address_line: input.addressLine ?? null,",
    "        p_city: input.city ?? null, p_region: input.region ?? null, p_postal_code: input.postalCode ?? null,",
    '        p_country: input.country ?? "US", p_visibility: input.visibility ?? null, p_source: input.source ?? "manual"',
    "      });",
    "    },",
    "    async nearMe(params: GeoSubjectRef & { targetTypes: string[]; radiusM: number; limit?: number }): Promise<GeoSearchHit[]> {",
    '      return rpc<GeoSearchHit[]>(opts, "geo_near_me", {',
    "        p_app_slug: params.appSlug, p_subject_type: params.subjectType, p_subject_id: params.subjectId,",
    "        p_target_types: params.targetTypes, p_radius_m: params.radiusM, p_limit: params.limit ?? 50",
    "      });",
    "    },",
    "    async clearPlace(ref: GeoSubjectRef): Promise<void> {",
    '      await rpc<null>(opts, "geo_clear_place", {',
    "        p_app_slug: ref.appSlug, p_subject_type: ref.subjectType, p_subject_id: ref.subjectId",
    "      });",
    "    }",
    "  };",
    "}"
  ]);
}

