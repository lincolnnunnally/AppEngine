// ---- src/app/api/location/route.ts ------------------------------------------

function locationApiFile(): GeneratedModuleFile {
  return file("src/app/api/location/route.ts", [
    'import { NextResponse } from "next/server";',
    'import { getCurrentUser } from "@/lib/auth/session";',
    'import { canAccessCustomerArea } from "@/lib/auth/roles";',
    'import { locationEnabled, geoConfigured, geoClient, appGeoSlug } from "@/lib/location";',
    'import { bandForMeters, milesToMeters, type GeoSearchHit } from "@/lib/geo/location-client";',
    "",
    'export const runtime = "nodejs";',
    'export const dynamic = "force-dynamic";',
    "",
    "function guard(user: { role?: string } | null) {",
    '  if (!locationEnabled()) return NextResponse.json({ ok: false, message: "Location is turned off for this app." }, { status: 404 });',
    '  if (!canAccessCustomerArea(user?.role)) return NextResponse.json({ ok: false, message: "Please sign in." }, { status: 401 });',
    '  if (!geoConfigured()) return NextResponse.json({ ok: false, message: "Location features aren\'t connected yet." }, { status: 503 });',
    "  return null;",
    "}",
    "",
    "// People/places near the signed-in user (bands only — never a raw distance).",
    "export async function GET(request: Request) {",
    "  const user = await getCurrentUser();",
    "  const blocked = guard(user);",
    "  if (blocked) return blocked;",
    "  const url = new URL(request.url);",
    '  const radiusMi = Number(url.searchParams.get("radiusMi") || "25");',
    '  const types = (url.searchParams.get("types") || "user").split(",").map((t) => t.trim()).filter(Boolean);',
    "  try {",
    "    const hits: GeoSearchHit[] = await geoClient().nearMe({",
    "      appSlug: appGeoSlug(),",
    '      subjectType: "user",',
    "      subjectId: user!.id,",
    "      targetTypes: types.length ? types : [\"user\"],",
    "      radiusM: milesToMeters(Math.min(50, Math.max(1, radiusMi)))",
    "    });",
    "    const nearby = hits.map((h) => ({ subjectType: h.subject_type, subjectId: h.subject_id, band: h.distance_band ?? bandForMeters(h.distance_m), cityLabel: h.city_label }));",
    "    return NextResponse.json({ ok: true, nearby });",
    "  } catch {",
    '    return NextResponse.json({ ok: false, message: "Could not search nearby right now." }, { status: 502 });',
    "  }",
    "}",
    "",
    "// Set the signed-in user's location (device coords or a city/postal fallback).",
    "export async function POST(request: Request) {",
    "  const user = await getCurrentUser();",
    "  const blocked = guard(user);",
    "  if (blocked) return blocked;",
    "  const body = (await request.json().catch(() => ({}))) as Record<string, unknown>;",
    '  const lat = typeof body.lat === "number" ? body.lat : undefined;',
    '  const lng = typeof body.lng === "number" ? body.lng : undefined;',
    '  const city = typeof body.city === "string" ? body.city : undefined;',
    '  const postalCode = typeof body.postalCode === "string" ? body.postalCode : undefined;',
    "  if (lat === undefined && !city && !postalCode) {",
    '    return NextResponse.json({ ok: false, message: "Share your location or enter a city / ZIP." }, { status: 400 });',
    "  }",
    "  try {",
    "    await geoClient().upsertPlace({",
    "      appSlug: appGeoSlug(),",
    '      subjectType: "user",',
    "      subjectId: user!.id,",
    "      lat, lng, city, postalCode,",
    '      source: lat !== undefined ? "device" : "manual",',
    '      visibility: "neighborhood"',
    "    });",
    "    return NextResponse.json({ ok: true });",
    "  } catch {",
    '    return NextResponse.json({ ok: false, message: "Could not save your location right now." }, { status: 502 });',
    "  }",
    "}",
    "",
    "// The honest \"clear my location\" contract: coordinates are genuinely removed.",
    "export async function DELETE() {",
    "  const user = await getCurrentUser();",
    "  const blocked = guard(user);",
    "  if (blocked) return blocked;",
    '  try { await geoClient().clearPlace({ appSlug: appGeoSlug(), subjectType: "user", subjectId: user!.id }); return NextResponse.json({ ok: true }); }',
    '  catch { return NextResponse.json({ ok: false, message: "Could not clear your location." }, { status: 502 }); }',
    "}"
  ]);
}

// ---- src/app/location/location-form.tsx (client: device opt-in + manual) ----

function locationFormFile(): GeneratedModuleFile {
  return file("src/app/location/location-form.tsx", [
    '"use client";',
    "",
    'import { useState } from "react";',
    "",
    "export function LocationForm() {",
    '  const [status, setStatus] = useState<string | null>(null);',
    "  const [busy, setBusy] = useState(false);",
    '  const [postalCode, setPostalCode] = useState("");',
    "",
    "  async function save(payload: Record<string, unknown>) {",
    "    setBusy(true);",
    "    setStatus(null);",
    "    try {",
    '      const res = await fetch("/api/location", { method: "POST", headers: { "content-type": "application/json" }, body: JSON.stringify(payload) });',
    "      const data = (await res.json().catch(() => ({}))) as { ok?: boolean; message?: string };",
    '      setStatus(data.ok ? "Saved. Refresh to see who is nearby." : (data.message || "Could not save."));',
    "    } catch {",
    '      setStatus("Network error. Try again.");',
    "    } finally {",
    "      setBusy(false);",
    "    }",
    "  }",
    "",
    "  function useMyLocation() {",
    "    if (!navigator.geolocation) {",
    '      setStatus("This device can\'t share location. Enter a ZIP instead.");',
    "      return;",
    "    }",
    "    setBusy(true);",
    "    navigator.geolocation.getCurrentPosition(",
    "      (pos) => save({ lat: pos.coords.latitude, lng: pos.coords.longitude }),",
    '      () => { setBusy(false); setStatus("Location permission was declined. Enter a ZIP instead."); }',
    "    );",
    "  }",
    "",
    "  async function clear() {",
    "    setBusy(true);",
    '    try { await fetch("/api/location", { method: "DELETE" }); setStatus("Your location was cleared."); } finally { setBusy(false); }',
    "  }",
    "",
    "  return (",
    '    <div className="stack">',
    '      <button className="button primary" type="button" onClick={useMyLocation} disabled={busy}>Use my current location</button>',
    '      <div className="stack">',
    '        <label className="signin-label" htmlFor="loc-zip">Or enter a ZIP / postal code</label>',
    '        <input id="loc-zip" className="input" value={postalCode} onChange={(e) => setPostalCode(e.target.value)} placeholder="e.g. 30014" disabled={busy} />',
    '        <button className="button" type="button" onClick={() => save({ postalCode })} disabled={busy || postalCode.trim().length < 3}>Save ZIP</button>',
    "      </div>",
    '      <button className="button" type="button" onClick={clear} disabled={busy}>Clear my location</button>',
    "      {status ? <p className=\"note\">{status}</p> : null}",
    "    </div>",
    "  );",
    "}"
  ]);
}

// ---- src/app/location/page.tsx (server-guarded near-me surface) --------------

function locationPageFile(): GeneratedModuleFile {
  return file("src/app/location/page.tsx", [
    'import { requireCustomerAccess, getCurrentUser } from "@/lib/auth/session";',
    'import { locationEnabled, geoConfigured, geoClient, appGeoSlug } from "@/lib/location";',
    'import { milesToMeters, bandForMeters } from "@/lib/geo/location-client";',
    'import { LocationForm } from "./location-form";',
    "",
    'export const dynamic = "force-dynamic";',
    "",
    "export default async function LocationPage() {",
    "  if (!locationEnabled()) {",
    '    return (<main className="shell"><h1>Near me</h1><p>Location is turned off for this app.</p></main>);',
    "  }",
    '  await requireCustomerAccess("/location");',
    "  const configured = geoConfigured();",
    "  let nearby: Array<{ subjectId: string; band: string | null; cityLabel: string | null }> = [];",
    "  if (configured) {",
    "    const user = await getCurrentUser();",
    "    try {",
    "      const hits = await geoClient().nearMe({",
    "        appSlug: appGeoSlug(),",
    '        subjectType: "user",',
    "        subjectId: user!.id,",
    '        targetTypes: ["user"],',
    "        radiusM: milesToMeters(25)",
    "      });",
    "      nearby = hits.map((h) => ({ subjectId: h.subject_id, band: h.distance_band ?? bandForMeters(h.distance_m), cityLabel: h.city_label }));",
    "    } catch {",
    "      nearby = [];",
    "    }",
    "  }",
    "",
    "  return (",
    '    <main className="shell">',
    '      <p className="eyebrow">Near me</p>',
    "      <h1>People near you</h1>",
    "      {!configured ? (",
    '        <p className="note">Location features aren\'t connected yet. Once the app owner connects the location service, you can share your neighborhood and find people near you — approximate distances only, never exact coordinates.</p>',
    "      ) : (",
    "        <>",
    "          <p>Share your location to find people near you. We only ever store an approximate, fuzzed point — never your exact spot — and show distance as a range.</p>",
    "          <LocationForm />",
    "          <section className=\"panel-list\">",
    "            {nearby.length === 0 ? (",
    '              <p className="note">No one nearby yet — or you haven\'t set your location above.</p>',
    "            ) : nearby.map((n) => (",
    '              <article className="wide-card" key={n.subjectId}>',
    '                <span>{n.band || "nearby"}</span>',
    "                <strong>{n.cityLabel || \"Someone nearby\"}</strong>",
    "              </article>",
    "            ))}",
    "          </section>",
    "        </>",
    "      )}",
    "    </main>",
    "  );",
    "}"
  ]);
}

