// media-recording — record video/audio in the browser, capture quick recordings,
// and keep a recordings library. Ported from the "honestly" app's real MediaRecorder
// flow (frontend/src/pages/QuickRecord.js + frontend/src/components/VideoRecorder.js
// + frontend/src/components/RecordingsTab.js) and its backend Recording model
// (backend/server.py class Recording / RecordingCreate / POST recordings):
//   - the Recording metadata model (id, case_id→collectionId, user_id→owner,
//     title, notes, duration [seconds], storage_path, media kind [audio/video],
//     mime type, is_analyzed, created_at). The capability brief is metadata-only —
//     "store metadata, no media infra required" — so we carry the record + the
//     MediaRecorder client logic and DROP the object-storage upload path (VideoRecorder
//     uploadVideo / QuickRecord multipart put_object). storage_path is kept as an
//     optional external URL the owner may paste.
//   - the QuickRecord recorder: getUserMedia({audio:{echoCancellation,noiseSuppression,
//     sampleRate}}) or ({video:{1280x720},audio:true}), MediaRecorder with
//     audio/webm;codecs=opus resp. video/webm;codecs=vp9,opus, start(1000), a 1-second
//     interval timer, pause/resume, stop → Blob → objectURL preview, discard, and the
//     formatTime mm:ss helper — all ported verbatim.
//   - the RecordingsTab library: list newest-first with duration + created date, a
//     play/pause toggle (here an inline <audio>/<video> element off the pasted URL),
//     an "analyzed" badge, and delete — ported from RecordingsTab's card list.
//   - the "assign to a case" selector (QuickRecord's case picker) generalized to an
//     optional free-text collection label so it drops into any app.
// Runs on the app owner's own database with graceful sample-data fallback,
// feature-flagged (FEATURE_RECORDING); customers record + manage their own recordings
// (rows scoped to user.id), admins can see + delete every recording.

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

