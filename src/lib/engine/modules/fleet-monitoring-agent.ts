    "// ---- Supply alerts (thresholds -> alerts) ---------------------------------",
    "// Ported from toner-management-app monitoring-triggers.ts evaluateSupplyTriggers:",
    "// per supply color, raise a critical_supply alert at/below the critical threshold",
    "// (resolving any low_supply first) or a low_supply alert at/below the low threshold,",
