// fleet-monitoring-agent — register customer devices, collect readings + heartbeats,
// detect device status, and surface fleet health. Ported from the real Toner
// monitoring stack:
//   - toner-management-app/src/server/agent.ts — the agent/device model + core logic:
//     requireAgent (install-key auth + revoked gate), normalizePlatform (win/mac/
//     linux/unknown), safeIpAddress / safeMacAddress validators, normalizeTonerLevels
//     (array-or-object -> {color, level_percent, pages_remaining, status}),
//     normalizePrinterFingerprint, and upsertPrinterFromPayload's identity resolution
//     (match existing device by serial_number, then fingerprint, then ip_address,
//     else insert; set status='online', bump last_monitored_at, merge metadata).
//   - toner-management-app/api/agent/[...path].ts — the agent ingest route surface.
//   - TonerTrackerPro/.../NetworkMonitor.tsx — the fleet view: online/offline/error
//     rollup counts, search across name/brand/ip, status filter, status icon/colour,
//     last-seen, and the stale->offline sweep (30s auto-refresh cadence).
//   - TonerTrackerPro/client/src/pages/monitoring-tool.tsx — the install-credentials
//     surface (install key + org id the agent uses to check in, copy-to-clipboard).
//   - toner-management-app/src/server/monitoring-triggers.ts (evaluateSupplyTriggers,
//     ported from TonerTrackerPro/server/toner-worker.ts) — the supply-threshold ALERT
//     engine: on every reading, raise a low_supply / critical_supply alert per supply
//     color and a device_offline / device_error connectivity alert, de-duplicated
//     against already-open alerts and auto-resolved when the device recovers. Thresholds
//     default to 30% (low) / 15% (critical), overridable via FLEET_LOW_SUPPLY_PERCENT /
//     FLEET_CRITICAL_SUPPLY_PERCENT. Fail-soft: alerting never breaks ingestion.
// Generalized off the printer/toner framing into plain "device / reading" language so
// it drops into any app. Runs on the app owner's own database with graceful sample-
// data fallback, feature-flagged (FEATURE_FLEET); customers register agents + read
// their own fleet, admins revoke agents. Every table is prefixed
// fleet_monitoring_agent_ and rows are scoped to the owning user(id).

import type { AppModule, GeneratedModuleFile } from "./types";

function file(path: string, lines: string[]): GeneratedModuleFile {
  return { path, content: lines.join("\n") + "\n" };
}

